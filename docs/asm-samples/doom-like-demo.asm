; PC-G815 Z80 ASM demo: Maze-style pseudo 3D
; 24-column lightweight raycast + fixed route auto-move.

ORG 0x0200
ENTRY START

LCD_CMD     EQU 0x58
LCD_DATA    EQU 0x5A

CMD_FWD     EQU 0x00
CMD_TURN_L  EQU 0x01
CMD_TURN_R  EQU 0x02
CMD_END     EQU 0xFF

DIR_NORTH   EQU 0x00
DIR_EAST    EQU 0x01
DIR_SOUTH   EQU 0x02
DIR_WEST    EQU 0x03

RAY_COUNT   EQU 24
DEPTH_MAX   EQU 6
DEPTH_LIMIT EQU 7
MOVE_INTERVAL EQU 1

START:
  LD A,0x01
  OUT (LCD_CMD),A

  LD A,1
  LD (POS_X),A
  LD A,1
  LD (POS_Y),A
  LD A,DIR_EAST
  LD (DIR),A
  XOR A
  LD (ROUTE_PTR),A
  LD (FRAME_TICK),A
  LD (MOVE_WAIT),A

MAIN_LOOP:
  CALL UPDATE_CAMERA
  CALL CAST_ALL_RAYS
  CALL RENDER_FRAME
  CALL BLIT_FRAME
  CALL FRAME_DELAY

  LD A,(FRAME_TICK)
  INC A
  LD (FRAME_TICK),A
  JR MAIN_LOOP

UPDATE_CAMERA:
  LD A,(MOVE_WAIT)
  INC A
  CP MOVE_INTERVAL
  JR C,WAIT_MORE
  XOR A
  LD (MOVE_WAIT),A
  JR UPDATE_ROUTE
WAIT_MORE:
  LD (MOVE_WAIT),A
  RET

UPDATE_ROUTE:
  LD A,(ROUTE_PTR)
  LD E,A
  LD D,0
  LD HL,ROUTE_TABLE
  ADD HL,DE
  LD A,(HL)
  CP CMD_END
  JR NZ,ROUTE_READY

  XOR A
  LD (ROUTE_PTR),A
  LD HL,ROUTE_TABLE
  LD A,(HL)

ROUTE_READY:
  CP CMD_FWD
  JR Z,ROUTE_FWD
  CP CMD_TURN_L
  JR Z,ROUTE_L
  CP CMD_TURN_R
  JR Z,ROUTE_R
  JR ROUTE_NEXT

ROUTE_FWD:
  CALL TRY_FORWARD
  JR ROUTE_NEXT

ROUTE_L:
  LD A,(DIR)
  DEC A
  AND 0x03
  LD (DIR),A
  JR ROUTE_NEXT

ROUTE_R:
  LD A,(DIR)
  INC A
  AND 0x03
  LD (DIR),A

ROUTE_NEXT:
  LD A,(ROUTE_PTR)
  INC A
  LD (ROUTE_PTR),A
  RET

TRY_FORWARD:
  LD A,(DIR)
  AND 0x03
  LD E,A
  LD D,0

  LD HL,FWD_DX
  ADD HL,DE
  LD A,(POS_X)
  ADD A,(HL)
  LD B,A

  LD HL,FWD_DY
  ADD HL,DE
  LD A,(POS_Y)
  ADD A,(HL)
  LD C,A

  CALL IS_WALL_BC
  OR A
  RET NZ

  LD A,B
  LD (POS_X),A
  LD A,C
  LD (POS_Y),A
  RET

CAST_ALL_RAYS:
  XOR A
  LD (RAY_INDEX),A

CAST_RAY_LOOP:
  LD A,(RAY_INDEX)
  CP RAY_COUNT
  RET NC

  CALL CAST_ONE_RAY

  LD A,(RAY_INDEX)
  INC A
  LD (RAY_INDEX),A
  JR CAST_RAY_LOOP

CAST_ONE_RAY:
  LD A,(RAY_INDEX)
  LD (CUR_RAY),A

  ; ptr = base + rayIndex * DEPTH_MAX (x6 => x4 + x2)
  LD A,(CUR_RAY)
  LD B,A
  ADD A,A
  ADD A,A
  LD C,A
  LD A,B
  ADD A,A
  ADD A,C
  LD E,A
  LD D,0

  LD HL,RAY_OFS_X
  ADD HL,DE
  LD (PTR_X),HL

  LD HL,RAY_OFS_Y
  ADD HL,DE
  LD (PTR_Y),HL

  LD A,1
  LD (CUR_DEPTH),A

CAST_DEPTH_LOOP:
  LD HL,(PTR_X)
  LD D,(HL)
  INC HL
  LD (PTR_X),HL

  LD HL,(PTR_Y)
  LD E,(HL)
  INC HL
  LD (PTR_Y),HL

  CALL ROTATE_DE

  LD A,(POS_X)
  ADD A,D
  LD B,A
  LD (HIT_X),A

  LD A,(POS_Y)
  ADD A,E
  LD C,A
  LD (HIT_Y),A

  CALL IS_WALL_BC
  OR A
  JR NZ,CAST_HIT

  LD A,(CUR_DEPTH)
  INC A
  LD (CUR_DEPTH),A
  CP DEPTH_LIMIT
  JR C,CAST_DEPTH_LOOP

  LD A,DEPTH_LIMIT
  LD (DEPTH_WORK),A
  XOR A
  LD (OPEN_SIDE),A
  JR CAST_STORE

CAST_HIT:
  LD A,(CUR_DEPTH)
  LD (DEPTH_WORK),A

  CALL CHECK_SIDE_OPEN
  LD (OPEN_SIDE),A

CAST_STORE:
  LD A,(CUR_RAY)
  LD E,A
  LD D,0

  LD HL,RAY_DEPTH
  ADD HL,DE
  LD A,(DEPTH_WORK)
  LD (HL),A

  LD HL,RAY_OPEN
  ADD HL,DE
  LD A,(OPEN_SIDE)
  LD (HL),A
  RET

CHECK_SIDE_OPEN:
  LD A,(CUR_RAY)
  CP 12
  JR C,CHECK_LEFT
  CALL SIDE_VEC_RIGHT
  JR CHECK_SIDE_VEC

CHECK_LEFT:
  CALL SIDE_VEC_LEFT

CHECK_SIDE_VEC:
  LD A,(HIT_X)
  ADD A,D
  LD B,A
  LD A,(HIT_Y)
  ADD A,E
  LD C,A
  CALL IS_WALL_BC
  OR A
  JR Z,SIDE_OPEN_YES
  XOR A
  RET

SIDE_OPEN_YES:
  LD A,1
  RET

ROTATE_DE:
  LD A,(DIR)
  AND 0x03
  CP DIR_NORTH
  RET Z
  CP DIR_EAST
  JR Z,ROT_EAST
  CP DIR_SOUTH
  JR Z,ROT_SOUTH

; WEST: (x,y) -> (y,-x)
ROT_WEST:
  LD A,D
  CPL
  INC A
  LD B,A
  LD A,E
  LD D,A
  LD E,B
  RET

; EAST: (x,y) -> (-y,x)
ROT_EAST:
  LD A,E
  CPL
  INC A
  LD B,A
  LD A,D
  LD E,A
  LD D,B
  RET

; SOUTH: (x,y) -> (-x,-y)
ROT_SOUTH:
  LD A,D
  CPL
  INC A
  LD D,A
  LD A,E
  CPL
  INC A
  LD E,A
  RET

SIDE_VEC_LEFT:
  LD A,(DIR)
  AND 0x03
  CP DIR_NORTH
  JR Z,SIDE_L_N
  CP DIR_EAST
  JR Z,SIDE_L_E
  CP DIR_SOUTH
  JR Z,SIDE_L_S
  ; WEST
  LD D,0x00
  LD E,0x01
  RET
SIDE_L_N:
  LD D,0xFF
  LD E,0x00
  RET
SIDE_L_E:
  LD D,0x00
  LD E,0xFF
  RET
SIDE_L_S:
  LD D,0x01
  LD E,0x00
  RET

SIDE_VEC_RIGHT:
  LD A,(DIR)
  AND 0x03
  CP DIR_NORTH
  JR Z,SIDE_R_N
  CP DIR_EAST
  JR Z,SIDE_R_E
  CP DIR_SOUTH
  JR Z,SIDE_R_S
  ; WEST
  LD D,0x00
  LD E,0xFF
  RET
SIDE_R_N:
  LD D,0x01
  LD E,0x00
  RET
SIDE_R_E:
  LD D,0x00
  LD E,0x01
  RET
SIDE_R_S:
  LD D,0xFF
  LD E,0x00
  RET

IS_WALL_BC:
  LD A,B
  CP 8
  JR NC,IS_WALL_TRUE
  LD A,C
  CP 8
  JR NC,IS_WALL_TRUE

  LD A,C
  ADD A,A
  ADD A,A
  ADD A,A
  ADD A,B
  LD L,A
  LD H,0
  LD DE,MAZE_DATA
  ADD HL,DE
  LD A,(HL)
  OR A
  RET

IS_WALL_TRUE:
  LD A,1
  RET

RENDER_FRAME:
  CALL CLEAR_FRAME
  CALL DETECT_OPENINGS
  CALL DRAW_BASE_FRAME
  CALL DRAW_ROUTE_PATTERN
  RET

CLEAR_FRAME:
  LD HL,FRAME_BUF
  LD B,96
  LD A,0x20
CLEAR_LOOP:
  LD (HL),A
  INC HL
  DJNZ CLEAR_LOOP
  RET

DETECT_OPENINGS:
  LD A,(DIR)
  CALL CHECK_OPEN_DIR
  LD (OPEN_FRONT),A

  LD A,(DIR)
  ADD A,3
  AND 0x03
  CALL CHECK_OPEN_DIR
  LD (OPEN_LEFT),A

  LD A,(DIR)
  INC A
  AND 0x03
  CALL CHECK_OPEN_DIR
  LD (OPEN_RIGHT),A
  RET

CHECK_OPEN_DIR:
  LD E,A
  LD D,0

  LD HL,FWD_DX
  ADD HL,DE
  LD A,(POS_X)
  ADD A,(HL)
  LD B,A

  LD HL,FWD_DY
  ADD HL,DE
  LD A,(POS_Y)
  ADD A,(HL)
  LD C,A

  CALL IS_WALL_BC
  OR A
  JR Z,OPEN_YES
  XOR A
  RET
OPEN_YES:
  LD A,1
  RET

DRAW_BASE_FRAME:
  ; ceiling and floor guides
  LD A,0x94
  LD B,0
  LD C,1
  LD D,22
  CALL DRAW_HLINE

  LD A,0x95
  LD B,3
  LD C,1
  LD D,22
  CALL DRAW_HLINE

  ; near left wall or opening
  LD A,(OPEN_LEFT)
  OR A
  JR NZ,BASE_LEFT_OPEN
  LD A,0x88
  LD B,1
  LD C,1
  LD D,2
  CALL DRAW_VLINE
  LD A,0xEF
  LD B,0
  LD C,1
  CALL PUT_XY
  JR BASE_RIGHT
BASE_LEFT_OPEN:
  LD A,0xEF
  LD B,0
  LD C,4
  CALL PUT_XY
  LD A,0x88
  LD B,1
  LD C,4
  LD D,2
  CALL DRAW_VLINE

BASE_RIGHT:
  ; near right wall or opening
  LD A,(OPEN_RIGHT)
  OR A
  JR NZ,BASE_RIGHT_OPEN
  LD A,0x97
  LD B,1
  LD C,22
  LD D,2
  CALL DRAW_VLINE
  LD A,0xEE
  LD B,0
  LD C,22
  CALL PUT_XY
  RET
BASE_RIGHT_OPEN:
  LD A,0xEE
  LD B,0
  LD C,19
  CALL PUT_XY
  LD A,0x97
  LD B,1
  LD C,19
  LD D,2
  CALL DRAW_VLINE
  RET

DRAW_ROUTE_PATTERN:
  LD A,(OPEN_FRONT)
  OR A
  JR Z,DRAW_FRONT_BLOCK
  CALL DRAW_FRONT_OPEN
  RET

DRAW_FRONT_BLOCK:
  ; front dead-end wall
  LD A,0x94
  LD B,1
  LD C,9
  LD D,6
  CALL DRAW_HLINE
  LD A,0x95
  LD B,2
  LD C,9
  LD D,6
  CALL DRAW_HLINE
  LD A,0x7C
  LD B,1
  LD C,9
  LD D,2
  CALL DRAW_VLINE
  LD A,0x7C
  LD B,1
  LD C,14
  LD D,2
  CALL DRAW_VLINE
  RET

DRAW_FRONT_OPEN:
  ; front corridor frame
  LD A,0x94
  LD B,0
  LD C,8
  LD D,8
  CALL DRAW_HLINE
  LD A,0x95
  LD B,2
  LD C,9
  LD D,6
  CALL DRAW_HLINE

  ; left far edge / opening hint
  LD A,(OPEN_LEFT)
  OR A
  JR NZ,FRONT_LEFT_OPEN
  LD A,0x88
  LD B,1
  LD C,8
  LD D,2
  CALL DRAW_VLINE
  JR FRONT_RIGHT
FRONT_LEFT_OPEN:
  LD A,0xEF
  LD B,0
  LD C,8
  CALL PUT_XY

FRONT_RIGHT:
  LD A,(OPEN_RIGHT)
  OR A
  JR NZ,FRONT_RIGHT_OPEN
  LD A,0x97
  LD B,1
  LD C,15
  LD D,2
  CALL DRAW_VLINE
  RET
FRONT_RIGHT_OPEN:
  LD A,0xEE
  LD B,0
  LD C,15
  CALL PUT_XY
  RET

DRAW_HLINE:
  LD E,A
DRAW_H_LOOP:
  LD A,D
  OR A
  RET Z
  PUSH DE
  LD A,E
  CALL PUT_XY
  POP DE
  INC C
  DEC D
  JR DRAW_H_LOOP

DRAW_VLINE:
  LD E,A
DRAW_V_LOOP:
  LD A,D
  OR A
  RET Z
  PUSH DE
  LD A,E
  CALL PUT_XY
  POP DE
  INC B
  DEC D
  JR DRAW_V_LOOP

PUT_XY:
  PUSH AF
  LD A,B
  LD E,A
  LD D,0
  LD HL,ROW_BASE
  ADD HL,DE
  LD A,(HL)
  ADD A,C
  LD L,A
  LD H,0
  LD DE,FRAME_BUF
  ADD HL,DE
  POP AF
  LD (HL),A
  RET

PUT_CELL:
  LD L,A
  LD H,0
  LD DE,FRAME_BUF
  ADD HL,DE
  LD (HL),B
  RET

PICK_CELL:
  ; in A=row
  LD C,A
  LD A,(TOP_ROW)
  CP 4
  JR Z,CELL_SKY
  CP C
  JR C,CELL_WALL
  JR Z,CELL_WALL
CELL_SKY:
  LD A,0x20
  RET

CELL_WALL:
  LD A,C
  LD B,A
  LD A,(TOP_ROW)
  CP B
  JR Z,CELL_TOP
  LD A,C
  CP 3
  JR NC,CELL_FACE

  LD A,(BOUNDARY_FLAG)
  OR A
  JR NZ,CELL_EDGE

  ; Inner vertical separators only on row2.
  LD A,C
  CP 2
  JR Z,CELL_FACE
  LD A,0x20
  RET

CELL_TOP:
  LD A,(OPEN_SIDE)
  OR A
  JR Z,CELL_CEIL_LINE

  LD A,(BOUNDARY_FLAG)
  OR A
  JR NZ,CELL_DIAG
  JR CELL_CEIL_LINE

CELL_EDGE:
  LD A,(EDGE_CHAR)
  RET

CELL_DIAG:
  LD A,(DIAG_CHAR)
  RET

CELL_CEIL_LINE:
  LD A,0x94
  RET

CELL_FACE:
  LD A,(FACE_CHAR)
  RET

PICK_TOP_ROW:
  LD A,(DEPTH_WORK)
  CP 2
  JR C,TOP0
  CP 3
  JR C,TOP1
  CP 4
  JR C,TOP2
  CP 6
  JR C,TOP3
  LD A,4
  RET
TOP0:
  LD A,0
  RET
TOP1:
  LD A,1
  RET
TOP2:
  LD A,2
  RET
TOP3:
  LD A,3
  RET

PICK_FACE_CHAR:
  LD A,(BOUNDARY_FLAG)
  OR A
  JR NZ,FACE_SPACE

  LD A,(DEPTH_WORK)
  CP 2
  JR C,FACE_STEP_NEAR
  CP 4
  JR C,FACE_STEP_MID
  CP 6
  JR C,FACE_STEP_FAR
  LD B,5
  JR FACE_STEP_TEST

FACE_STEP_NEAR:
  LD B,8
  JR FACE_STEP_TEST

FACE_STEP_MID:
  LD B,7
  JR FACE_STEP_TEST

FACE_STEP_FAR:
  LD B,6

FACE_STEP_TEST:
  CALL IS_RAY_MULTIPLE
  JR Z,FACE_SEP

FACE_SPACE:
  LD A,0x20
  RET

FACE_SEP:
  LD A,0x7C
  RET

PICK_EDGE_CHAR:
  LD A,(RAY_INDEX)
  CP 12
  JR C,EDGE_LEFT
  LD A,0x97
  RET
EDGE_LEFT:
  LD A,0x88
  RET

PICK_DIAG_CHAR:
  LD A,(RAY_INDEX)
  CP 12
  JR C,DIAG_L
  LD A,0xEE
  RET
DIAG_L:
  LD A,0xEF
  RET

PICK_FLOOR_CHAR:
  LD A,0x95
  RET

IS_RAY_MULTIPLE:
  LD A,(RAY_INDEX)
MULTIPLE_LOOP:
  CP B
  JR C,MULTIPLE_DONE
  SUB B
  JR MULTIPLE_LOOP
MULTIPLE_DONE:
  OR A
  RET

COMPUTE_BOUNDARY:
  LD A,(RAY_INDEX)
  OR A
  JR Z,BOUNDARY_YES
  CP 23
  JR Z,BOUNDARY_YES

  LD D,A
  DEC A
  CALL GET_DEPTH_AT
  LD B,A

  LD A,D
  CALL GET_DEPTH_AT
  LD C,A

  LD A,B
  CP C
  JR Z,BOUNDARY_NO

  LD A,D
  INC A
  CALL GET_DEPTH_AT
  CP C
  JR Z,BOUNDARY_NO

BOUNDARY_YES:
  LD A,1
  RET

BOUNDARY_NO:
  XOR A
  RET

GET_DEPTH_AT:
  LD E,A
  LD D,0
  LD HL,RAY_DEPTH
  ADD HL,DE
  LD A,(HL)
  RET

BLIT_FRAME:
  LD A,0x80
  OUT (LCD_CMD),A
  LD HL,FRAME_BUF
  LD B,96
BLIT_LOOP:
  LD A,(HL)
  OUT (LCD_DATA),A
  INC HL
  DJNZ BLIT_LOOP
  RET

FRAME_DELAY:
  LD BC,0x2200
DELAY_LOOP:
  DEC BC
  LD A,B
  OR C
  JR NZ,DELAY_LOOP
  RET

POS_X:
  DB 0
POS_Y:
  DB 0
DIR:
  DB 0
ROUTE_PTR:
  DB 0
FRAME_TICK:
  DB 0
MOVE_WAIT:
  DB 0

RAY_INDEX:
  DB 0
CUR_RAY:
  DB 0
CUR_DEPTH:
  DB 0
DEPTH_WORK:
  DB 0
OPEN_SIDE:
  DB 0
TOP_ROW:
  DB 0
FACE_CHAR:
  DB 0
EDGE_CHAR:
  DB 0
DIAG_CHAR:
  DB 0
BOUNDARY_FLAG:
  DB 0
OPEN_FRONT:
  DB 0
OPEN_LEFT:
  DB 0
OPEN_RIGHT:
  DB 0
HIT_X:
  DB 0
HIT_Y:
  DB 0
PTR_X:
  DW 0
PTR_Y:
  DW 0

RAY_DEPTH:
  DS 24,0
RAY_OPEN:
  DS 24,0
FRAME_BUF:
  DS 96,0x20
ROW_BASE:
  DB 0,24,48,72

FWD_DX:
  DB 0,1,0,0xFF
FWD_DY:
  DB 0xFF,0,1,0

; 24 rays x 6 depths (north basis)
