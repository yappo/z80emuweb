DRAW_LINE:
  LD A,B
  LD (LINE_X),A
  LD A,C
  LD (LINE_Y),A
  LD A,D
  LD (LINE_X_END),A
  LD A,E
  LD (LINE_Y_END),A

  LD A,D
  SUB B
  JP NC,DRAW_LINE_DX_POS
  CPL
  INC A
  LD (LINE_DX),A
  LD A,0xFF
  LD (LINE_SX),A
  JP DRAW_LINE_DY
