  LD A,(FAR_BOTTOM)
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  LD A,B
  SUB C
  SRL A
  SRL A
  JR NZ,COMPUTE_BRANCH_END_HAVE_INSET
  INC A
COMPUTE_BRANCH_END_HAVE_INSET:
  LD B,A
  ADD A,A
  LD (BRANCH_END_GAP),A
  LD A,B
  LD A,(FAR_TOP)
  ADD A,B
  LD (BRANCH_END_TOP),A
  LD A,(FAR_BOTTOM)
  SUB B
  LD (BRANCH_END_BOTTOM),A
  RET


