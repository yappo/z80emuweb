SET_PIXEL:
  LD A,C
  CP 32
  RET NC

  LD A,B
  CP HALF_WIDTH
  JP C,SET_PIXEL_LEFT
  LD A,SCREEN_RIGHT
  SUB B
  LD (PIXEL_RAW_X),A
  LD A,C
  SRL A
  SRL A
  SRL A
