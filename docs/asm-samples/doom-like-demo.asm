; PC-G815 Z80 ASM demo: DOOM-like wireframe maze
; 144x32 dot graphics, shadow VRAM, dirty raw-byte LCD flush, autoplay route.

ORG 0x0200
ENTRY START

LCD_CMD2      EQU 0x54
LCD_DATA2     EQU 0x56
LCD_CMD       EQU 0x58
LCD_DATA      EQU 0x5A

CMD_FWD       EQU 0x00
CMD_TURN_L    EQU 0x01
CMD_TURN_R    EQU 0x02
CMD_END       EQU 0xFF

DIR_NORTH     EQU 0x00
DIR_EAST      EQU 0x01
DIR_SOUTH     EQU 0x02
DIR_WEST      EQU 0x03

MAZE_SIZE      EQU 10
MAX_DEPTH     EQU 4
MOVE_INTERVAL EQU 3
SCREEN_RIGHT  EQU 143
HALF_WIDTH    EQU 72
RAW_STRIDE    EQU 72
RAW_BYTES     EQU 576

START:
  CALL INIT_VIDEO

  LD A,2
  LD (POS_X),A
  LD A,1
  LD (POS_Y),A
  LD A,DIR_EAST
  LD (DIR),A
  XOR A
  LD (FRAME_TICK),A
  LD (MOVE_WAIT),A

MAIN_LOOP:
  CALL UPDATE_CAMERA
  CALL BUILD_SCENE
  CALL CLEAR_SHADOW_VRAM
  CALL RENDER_SCENE
  CALL FLUSH_DIRTY_VRAM
  CALL FRAME_DELAY

  LD A,(FRAME_TICK)
  INC A
  LD (FRAME_TICK),A
  JP MAIN_LOOP

INIT_VIDEO:
  CALL CLEAR_SHADOW_VRAM
  CALL FILL_PREV_VRAM
  CALL FLUSH_DIRTY_VRAM
  RET

UPDATE_CAMERA:
  LD A,(MOVE_WAIT)
  INC A
  CP MOVE_INTERVAL
  JP C,WAIT_MORE
  XOR A
  LD (MOVE_WAIT),A
  JP AUTO_STEP
WAIT_MORE:
  LD (MOVE_WAIT),A
  RET

AUTO_STEP:
  CALL CAN_MOVE_LEFT
  OR A
  JP Z,AUTO_TRY_FORWARD
  CALL TURN_LEFT
  CALL TRY_FORWARD
  RET

AUTO_TRY_FORWARD:
  CALL CAN_MOVE_FORWARD
  OR A
  JP Z,AUTO_TRY_RIGHT
  CALL TRY_FORWARD
  RET

AUTO_TRY_RIGHT:
  CALL CAN_MOVE_RIGHT
  OR A
  JP Z,AUTO_TURN_BACK
  CALL TURN_RIGHT
  CALL TRY_FORWARD
  RET

AUTO_TURN_BACK:
  CALL TURN_BACK
  CALL TRY_FORWARD
  RET

TRY_FORWARD:
  LD A,(DIR)
  AND 0x03
  LD E,A
  LD D,0

  LD HL,FWD_DX
  ADD HL,DE
  LD A,(POS_X)
  ADD A,(HL)
  LD B,A

  LD HL,FWD_DY
  ADD HL,DE
  LD A,(POS_Y)
  ADD A,(HL)
  LD C,A

  CALL IS_WALL_BC
  OR A
  RET NZ

  LD A,B
  LD (POS_X),A
  LD A,C
  LD (POS_Y),A
  RET

TURN_LEFT:
  LD A,(DIR)
  DEC A
  AND 0x03
  LD (DIR),A
  RET

TURN_RIGHT:
  LD A,(DIR)
  INC A
  AND 0x03
  LD (DIR),A
  RET

TURN_BACK:
  LD A,(DIR)
  ADD A,2
  AND 0x03
  LD (DIR),A
  RET

CAN_MOVE_FORWARD:
  LD A,(DIR)
  AND 0x03
  LD E,A
  LD D,0
  LD HL,FWD_DX
  ADD HL,DE
  LD D,(HL)
  LD HL,FWD_DY
  ADD HL,DE
  LD E,(HL)
  JP CHECK_STEP_VECTOR

CAN_MOVE_LEFT:
  CALL SIDE_VEC_LEFT
  JP CHECK_STEP_VECTOR

CAN_MOVE_RIGHT:
  CALL SIDE_VEC_RIGHT
  JP CHECK_STEP_VECTOR

CHECK_STEP_VECTOR:
  LD A,(POS_X)
  ADD A,D
  LD B,A
  LD A,(POS_Y)
  ADD A,E
  LD C,A
  CALL IS_WALL_BC
  OR A
  JP Z,CHECK_STEP_OPEN
  XOR A
  RET
CHECK_STEP_OPEN:
  LD A,1
  RET

BUILD_SCENE:
  XOR A
  LD (SCENE_FRONT_HIT),A
  LD (SCENE_FRONT_DEPTH),A
  LD (SCENE_LEFT_OPEN+0),A
  LD (SCENE_LEFT_OPEN+1),A
  LD (SCENE_LEFT_OPEN+2),A
  LD (SCENE_LEFT_OPEN+3),A
  LD (SCENE_RIGHT_OPEN+0),A
  LD (SCENE_RIGHT_OPEN+1),A
  LD (SCENE_RIGHT_OPEN+2),A
  LD (SCENE_RIGHT_OPEN+3),A
  LD (SCENE_LEFT_LEN+0),A
  LD (SCENE_LEFT_LEN+1),A
  LD (SCENE_LEFT_LEN+2),A
  LD (SCENE_LEFT_LEN+3),A
  LD (SCENE_RIGHT_LEN+0),A
  LD (SCENE_RIGHT_LEN+1),A
  LD (SCENE_RIGHT_LEN+2),A
  LD (SCENE_RIGHT_LEN+3),A

  LD A,(POS_X)
  LD (TEMP_X),A
  LD A,(POS_Y)
  LD (TEMP_Y),A
  LD A,1
  LD (CUR_DEPTH),A

BUILD_SCENE_LOOP:
  LD A,(CUR_DEPTH)
  CP MAX_DEPTH + 1
  JP NC,BUILD_SCENE_OPEN

  LD A,(DIR)
  AND 0x03
  LD E,A
  LD D,0

  LD HL,FWD_DX
  ADD HL,DE
  LD A,(TEMP_X)
  ADD A,(HL)
  LD (TEMP_X),A
  LD B,A

  LD HL,FWD_DY
  ADD HL,DE
  LD A,(TEMP_Y)
  ADD A,(HL)
  LD (TEMP_Y),A
  LD C,A

  CALL IS_WALL_BC
  OR A
  JP NZ,BUILD_SCENE_HIT

  CALL CHECK_TEMP_LEFT_OPEN
  CALL STORE_LEFT_FLAG
  OR A
  JR Z,BUILD_SCENE_SKIP_LEFT_LEN
  CALL MEASURE_TEMP_LEFT_LEN
  CALL STORE_LEFT_LEN
BUILD_SCENE_SKIP_LEFT_LEN:
  CALL CHECK_TEMP_RIGHT_OPEN
  CALL STORE_RIGHT_FLAG
  OR A
  JR Z,BUILD_SCENE_SKIP_RIGHT_LEN
  CALL MEASURE_TEMP_RIGHT_LEN
  CALL STORE_RIGHT_LEN
BUILD_SCENE_SKIP_RIGHT_LEN:

  LD A,(CUR_DEPTH)
  INC A
  LD (CUR_DEPTH),A
  JP BUILD_SCENE_LOOP

BUILD_SCENE_HIT:
  LD A,1
  LD (SCENE_FRONT_HIT),A
  LD A,(CUR_DEPTH)
  LD (SCENE_FRONT_DEPTH),A
  RET

BUILD_SCENE_OPEN:
  LD A,MAX_DEPTH
  LD (SCENE_FRONT_DEPTH),A
  RET

STORE_LEFT_FLAG:
  PUSH AF
  LD A,(CUR_DEPTH)
  DEC A
  LD E,A
  LD D,0
  LD HL,SCENE_LEFT_OPEN
  ADD HL,DE
  POP AF
  LD (HL),A
  RET

STORE_RIGHT_FLAG:
  PUSH AF
  LD A,(CUR_DEPTH)
  DEC A
  LD E,A
  LD D,0
  LD HL,SCENE_RIGHT_OPEN
  ADD HL,DE
  POP AF
  LD (HL),A
  RET

STORE_LEFT_LEN:
  PUSH AF
  LD A,(CUR_DEPTH)
  DEC A
  LD E,A
  LD D,0
  LD HL,SCENE_LEFT_LEN
  ADD HL,DE
  POP AF
  LD (HL),A
  RET

STORE_RIGHT_LEN:
  PUSH AF
  LD A,(CUR_DEPTH)
  DEC A
  LD E,A
  LD D,0
  LD HL,SCENE_RIGHT_LEN
  ADD HL,DE
  POP AF
  LD (HL),A
  RET

CHECK_TEMP_LEFT_OPEN:
  CALL SIDE_VEC_LEFT
  JP CHECK_TEMP_SIDE_VECTOR

CHECK_TEMP_RIGHT_OPEN:
  CALL SIDE_VEC_RIGHT

CHECK_TEMP_SIDE_VECTOR:
  LD A,(TEMP_X)
  ADD A,D
  LD B,A
  LD A,(TEMP_Y)
  ADD A,E
  LD C,A
  CALL IS_WALL_BC
  OR A
  JP Z,CHECK_TEMP_SIDE_OPEN
  XOR A
  RET
CHECK_TEMP_SIDE_OPEN:
  LD A,1
  RET

MEASURE_TEMP_LEFT_LEN:
  CALL SIDE_VEC_LEFT
  JP MEASURE_TEMP_SIDE_LEN

MEASURE_TEMP_RIGHT_LEN:
  CALL SIDE_VEC_RIGHT

MEASURE_TEMP_SIDE_LEN:
  LD A,(TEMP_X)
  LD (BRANCH_X),A
  LD A,(TEMP_Y)
  LD (BRANCH_Y),A
  LD A,MAX_DEPTH
  LD (BRANCH_LIMIT),A
  XOR A
  LD (BRANCH_LEN),A

MEASURE_TEMP_SIDE_LOOP:
  LD A,(BRANCH_X)
  ADD A,D
  LD (BRANCH_X),A
  LD B,A
  LD A,(BRANCH_Y)
  ADD A,E
  LD (BRANCH_Y),A
  LD C,A
  PUSH DE
  CALL IS_WALL_BC
  POP DE
  OR A
  JR NZ,MEASURE_TEMP_SIDE_DONE
  LD A,(BRANCH_LEN)
  INC A
  LD (BRANCH_LEN),A
  LD B,A
  LD A,(BRANCH_LIMIT)
  CP B
  JR Z,MEASURE_TEMP_SIDE_DONE
  JR C,MEASURE_TEMP_SIDE_DONE
  JR MEASURE_TEMP_SIDE_LOOP

MEASURE_TEMP_SIDE_DONE:
  LD A,(BRANCH_LEN)
  RET

RENDER_SCENE:
  CALL DRAW_LEFT_WALL
  CALL DRAW_RIGHT_WALL
  CALL DRAW_LEFT_BRANCHES
  CALL DRAW_RIGHT_BRANCHES

  LD A,(SCENE_FRONT_HIT)
  OR A
  RET Z
  CALL COMPUTE_FRONT_CLIP
  CALL DRAW_FAR_RECT
  CALL DRAW_FAR_TILES
  RET

LOAD_NEAR_RECT:
  LD (RECT_INDEX),A
  LD HL,RECT_LEFTS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (NEAR_LEFT),A
  LD HL,RECT_RIGHTS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (NEAR_RIGHT),A
  LD HL,RECT_TOPS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (NEAR_TOP),A
  LD HL,RECT_BOTTOMS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (NEAR_BOTTOM),A
  RET

LOAD_FAR_RECT:
  LD (RECT_INDEX),A
  LD HL,RECT_LEFTS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (FAR_LEFT),A
  LD HL,RECT_RIGHTS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (FAR_RIGHT),A
  LD HL,RECT_TOPS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (FAR_TOP),A
  LD HL,RECT_BOTTOMS
  LD A,(RECT_INDEX)
  CALL GET_TABLE_VALUE
  LD (FAR_BOTTOM),A
  RET

GET_TABLE_VALUE:
  LD E,A
  LD D,0
  ADD HL,DE
  LD A,(HL)
  RET

LOAD_LEFT_FLAG:
  PUSH BC
  LD E,A
  DEC E
  LD D,0
  LD HL,SCENE_LEFT_OPEN
  ADD HL,DE
  LD A,(HL)
  POP BC
  RET

LOAD_RIGHT_FLAG:
  PUSH BC
  LD E,A
  DEC E
  LD D,0
  LD HL,SCENE_RIGHT_OPEN
  ADD HL,DE
  LD A,(HL)
  POP BC
  RET

LOAD_LEFT_LEN:
  PUSH BC
  LD E,A
  DEC E
  LD D,0
  LD HL,SCENE_LEFT_LEN
  ADD HL,DE
  LD A,(HL)
  POP BC
  RET

LOAD_RIGHT_LEN:
  PUSH BC
  LD E,A
  DEC E
  LD D,0
  LD HL,SCENE_RIGHT_LEN
  ADD HL,DE
  LD A,(HL)
  POP BC
  RET

FIND_FIRST_LEFT_OPEN:
  LD B,1
FIND_FIRST_LEFT_OPEN_LOOP:
  LD A,B
  CALL LOAD_LEFT_FLAG
  OR A
  JR NZ,FIND_FIRST_LEFT_OPEN_FOUND
  INC B
  LD A,B
  CP MAX_DEPTH + 1
  JR C,FIND_FIRST_LEFT_OPEN_LOOP
  XOR A
  RET
FIND_FIRST_LEFT_OPEN_FOUND:
  LD A,B
  RET

FIND_FIRST_RIGHT_OPEN:
  LD B,1
FIND_FIRST_RIGHT_OPEN_LOOP:
  LD A,B
  CALL LOAD_RIGHT_FLAG
  OR A
  JR NZ,FIND_FIRST_RIGHT_OPEN_FOUND
  INC B
  LD A,B
  CP MAX_DEPTH + 1
  JR C,FIND_FIRST_RIGHT_OPEN_LOOP
  XOR A
  RET
FIND_FIRST_RIGHT_OPEN_FOUND:
  LD A,B
  RET

DRAW_LEFT_WALL:
  LD A,(SCENE_FRONT_HIT)
  OR A
  JR Z,DRAW_LEFT_WALL_OPEN_LIMIT
  LD A,(SCENE_FRONT_DEPTH)
  JR DRAW_LEFT_WALL_HAVE_LIMIT
DRAW_LEFT_WALL_OPEN_LIMIT:
  LD A,MAX_DEPTH
DRAW_LEFT_WALL_HAVE_LIMIT:
  PUSH AF
  CALL DRAW_LEFT_SEGMENTS
  POP AF
  CALL DRAW_LEFT_SEAMS
  RET

DRAW_RIGHT_WALL:
  LD A,(SCENE_FRONT_HIT)
  OR A
  JR Z,DRAW_RIGHT_WALL_OPEN_LIMIT
  LD A,(SCENE_FRONT_DEPTH)
  JR DRAW_RIGHT_WALL_HAVE_LIMIT
DRAW_RIGHT_WALL_OPEN_LIMIT:
  LD A,MAX_DEPTH
DRAW_RIGHT_WALL_HAVE_LIMIT:
  PUSH AF
  CALL DRAW_RIGHT_SEGMENTS
  POP AF
  CALL DRAW_RIGHT_SEAMS
  RET

DRAW_LEFT_SEGMENTS:
  OR A
  RET Z
  LD (RENDER_LIMIT),A
  LD A,1
DRAW_LEFT_SEGMENTS_LOOP:
  PUSH AF
  CP 1
  JR Z,DRAW_LEFT_SEGMENTS_NO_PREV_OPEN
  DEC A
  CALL LOAD_LEFT_FLAG
  OR A
  JR NZ,DRAW_LEFT_SEGMENTS_SKIP_POP2
DRAW_LEFT_SEGMENTS_NO_PREV_OPEN:
  POP AF
  PUSH AF
  DEC A
  CALL LOAD_NEAR_RECT
  POP AF
  PUSH AF
  CALL LOAD_FAR_RECT
  CALL DRAW_LEFT_EDGE_LINES
  POP AF
  JR DRAW_LEFT_SEGMENTS_NEXT
DRAW_LEFT_SEGMENTS_SKIP_POP2:
  POP AF
  JR DRAW_LEFT_SEGMENTS_NEXT
DRAW_LEFT_SEGMENTS_NEXT:
  INC A
  LD B,A
  LD A,(RENDER_LIMIT)
  CP B
  LD A,B
  JR NC,DRAW_LEFT_SEGMENTS_LOOP
  RET

DRAW_RIGHT_SEGMENTS:
  OR A
  RET Z
  LD (RENDER_LIMIT),A
  LD A,1
DRAW_RIGHT_SEGMENTS_LOOP:
  PUSH AF
  CP 1
  JR Z,DRAW_RIGHT_SEGMENTS_NO_PREV_OPEN
  DEC A
  CALL LOAD_RIGHT_FLAG
  OR A
  JR NZ,DRAW_RIGHT_SEGMENTS_SKIP_POP2
DRAW_RIGHT_SEGMENTS_NO_PREV_OPEN:
  POP AF
  PUSH AF
  DEC A
  CALL LOAD_NEAR_RECT
  POP AF
  PUSH AF
  CALL LOAD_FAR_RECT
  CALL DRAW_RIGHT_EDGE_LINES
  POP AF
  JR DRAW_RIGHT_SEGMENTS_NEXT
DRAW_RIGHT_SEGMENTS_SKIP_POP2:
  POP AF
  JR DRAW_RIGHT_SEGMENTS_NEXT
DRAW_RIGHT_SEGMENTS_NEXT:
  INC A
  LD B,A
  LD A,(RENDER_LIMIT)
  CP B
  LD A,B
  JR NC,DRAW_RIGHT_SEGMENTS_LOOP
  RET

DRAW_LEFT_EDGE_LINES:
  LD A,(NEAR_LEFT)
  LD B,A
  LD A,(NEAR_TOP)
  LD C,A
  LD A,(FAR_LEFT)
  LD D,A
  LD A,(FAR_TOP)
  LD E,A
  CALL DRAW_LINE

  LD A,(NEAR_LEFT)
  LD B,A
  LD A,(NEAR_BOTTOM)
  LD C,A
  LD A,(FAR_LEFT)
  LD D,A
  LD A,(FAR_BOTTOM)
  LD E,A
  CALL DRAW_LINE
  RET

DRAW_RIGHT_EDGE_LINES:
  LD A,(NEAR_RIGHT)
  LD B,A
  LD A,(NEAR_TOP)
  LD C,A
  LD A,(FAR_RIGHT)
  LD D,A
  LD A,(FAR_TOP)
  LD E,A
  CALL DRAW_LINE

  LD A,(NEAR_RIGHT)
  LD B,A
  LD A,(NEAR_BOTTOM)
  LD C,A
  LD A,(FAR_RIGHT)
  LD D,A
  LD A,(FAR_BOTTOM)
  LD E,A
  CALL DRAW_LINE
  RET

DRAW_LEFT_SEAMS:
  CP 1
  RET C
  LD (RENDER_LIMIT),A
  LD A,1
DRAW_LEFT_SEAMS_LOOP:
  PUSH AF
  CALL LOAD_FAR_RECT
  CALL DRAW_LEFT_RECT_EDGE
  POP AF
  INC A
  LD B,A
  LD A,(RENDER_LIMIT)
  CP B
  LD A,B
  JR NC,DRAW_LEFT_SEAMS_LOOP
  RET

DRAW_RIGHT_SEAMS:
  CP 1
  RET C
  LD (RENDER_LIMIT),A
  LD A,1
DRAW_RIGHT_SEAMS_LOOP:
  PUSH AF
  CALL LOAD_FAR_RECT
  CALL DRAW_RIGHT_RECT_EDGE
  POP AF
  INC A
  LD B,A
  LD A,(RENDER_LIMIT)
  CP B
  LD A,B
  JR NC,DRAW_RIGHT_SEAMS_LOOP
  RET

DRAW_LEFT_RECT_EDGE:
  LD A,(FAR_LEFT)
  DEC A
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  CALL SET_PIXEL

  LD A,(FAR_LEFT)
  DEC A
  LD B,A
  LD A,(FAR_BOTTOM)
  LD C,A
  CALL SET_PIXEL

  LD A,(FAR_LEFT)
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  LD A,(FAR_LEFT)
  LD D,A
  LD A,(FAR_BOTTOM)
  LD E,A
  CALL DRAW_LINE
  RET

DRAW_RIGHT_RECT_EDGE:
  LD A,(FAR_RIGHT)
  INC A
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  CALL SET_PIXEL

  LD A,(FAR_RIGHT)
  INC A
  LD B,A
  LD A,(FAR_BOTTOM)
  LD C,A
  CALL SET_PIXEL

  LD A,(FAR_RIGHT)
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  LD A,(FAR_RIGHT)
  LD D,A
  LD A,(FAR_BOTTOM)
  LD E,A
  CALL DRAW_LINE
  RET

DRAW_LEFT_BRANCHES:
  LD A,(SCENE_FRONT_DEPTH)
  LD (RENDER_LIMIT),A
  LD A,1
DRAW_LEFT_BRANCHES_LOOP:
  PUSH AF
  CALL LOAD_LEFT_LEN
  OR A
  JR Z,DRAW_LEFT_BRANCHES_SKIP_POP
  POP AF
  PUSH AF
  CALL DRAW_LEFT_BRANCH_AT_DEPTH
  POP AF
  JR DRAW_LEFT_BRANCHES_NEXT
DRAW_LEFT_BRANCHES_SKIP_POP:
  POP AF
DRAW_LEFT_BRANCHES_NEXT:
  INC A
  LD B,A
  LD A,(RENDER_LIMIT)
  CP B
  LD A,B
  JR NC,DRAW_LEFT_BRANCHES_LOOP
  RET

DRAW_RIGHT_BRANCHES:
  LD A,(SCENE_FRONT_DEPTH)
  LD (RENDER_LIMIT),A
  LD A,1
DRAW_RIGHT_BRANCHES_LOOP:
  PUSH AF
  CALL LOAD_RIGHT_LEN
  OR A
  JR Z,DRAW_RIGHT_BRANCHES_SKIP_POP
  POP AF
  PUSH AF
  CALL DRAW_RIGHT_BRANCH_AT_DEPTH
  POP AF
  JR DRAW_RIGHT_BRANCHES_NEXT
DRAW_RIGHT_BRANCHES_SKIP_POP:
  POP AF
DRAW_RIGHT_BRANCHES_NEXT:
  INC A
  LD B,A
  LD A,(RENDER_LIMIT)
  CP B
  LD A,B
  JR NC,DRAW_RIGHT_BRANCHES_LOOP
  RET

DRAW_LEFT_BRANCH_AT_DEPTH:
  LD (RECT_INDEX),A
  CALL LOAD_LEFT_LEN
  LD B,A
  LD A,(RECT_INDEX)
  CP B
  JR C,DRAW_LEFT_BRANCH_OUTER_ZERO
DRAW_LEFT_BRANCH_HAVE_OUTER:
  SUB B
  JP P,DRAW_LEFT_BRANCH_OUTER_READY
DRAW_LEFT_BRANCH_OUTER_ZERO:
  XOR A
  JR DRAW_LEFT_BRANCH_OUTER_READY
DRAW_LEFT_BRANCH_OUTER_READY:
  LD (BRANCH_OUTER),A
  LD A,(BRANCH_OUTER)
  OR A
  JR Z,DRAW_LEFT_BRANCH_VISIBLE_CONTINUE
  LD A,(RECT_INDEX)
  CALL LOAD_FAR_RECT
  LD A,(BRANCH_OUTER)
  CALL LOAD_NEAR_RECT

  CALL COMPUTE_BRANCH_END_BOUNDS
  LD A,(FAR_LEFT)
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  LD A,(NEAR_LEFT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  ADD A,E
  LD D,A
  LD A,(BRANCH_END_TOP)
  LD E,A
  CALL DRAW_LINE

  LD A,(FAR_LEFT)
  LD B,A
  LD A,(FAR_BOTTOM)
  LD C,A
  LD A,(NEAR_LEFT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  ADD A,E
  LD D,A
  LD A,(BRANCH_END_BOTTOM)
  LD E,A
  CALL DRAW_LINE

  LD A,(BRANCH_OUTER)
  CALL LOAD_FAR_RECT
  CALL DRAW_LEFT_RECT_EDGE
  RET

DRAW_LEFT_BRANCH_VISIBLE_CONTINUE:
  LD A,(RECT_INDEX)
  CP MAX_DEPTH
  RET NC
  INC A
  PUSH AF
  CALL LOAD_FAR_RECT
  POP AF
  DEC A
  CALL LOAD_NEAR_RECT

  CALL COMPUTE_BRANCH_END_BOUNDS
  LD A,(FAR_LEFT)
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  LD A,(NEAR_LEFT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  ADD A,E
  LD D,A
  LD A,(BRANCH_END_TOP)
  LD E,A
  CALL DRAW_LINE

  LD A,(FAR_LEFT)
  LD B,A
  LD A,(FAR_BOTTOM)
  LD C,A
  LD A,(NEAR_LEFT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  ADD A,E
  LD D,A
  LD A,(BRANCH_END_BOTTOM)
  LD E,A
  CALL DRAW_LINE
  RET

DRAW_RIGHT_BRANCH_AT_DEPTH:
  LD (RECT_INDEX),A
  CALL LOAD_RIGHT_LEN
  LD B,A
  LD A,(RECT_INDEX)
  CP B
  JR C,DRAW_RIGHT_BRANCH_OUTER_ZERO
DRAW_RIGHT_BRANCH_HAVE_OUTER:
  SUB B
  JP P,DRAW_RIGHT_BRANCH_OUTER_READY
DRAW_RIGHT_BRANCH_OUTER_ZERO:
  XOR A
  JR DRAW_RIGHT_BRANCH_OUTER_READY
DRAW_RIGHT_BRANCH_OUTER_READY:
  LD (BRANCH_OUTER),A
  LD A,(BRANCH_OUTER)
  OR A
  JR Z,DRAW_RIGHT_BRANCH_VISIBLE_CONTINUE
  LD A,(RECT_INDEX)
  CALL LOAD_FAR_RECT
  LD A,(BRANCH_OUTER)
  CALL LOAD_NEAR_RECT

  CALL COMPUTE_BRANCH_END_BOUNDS
  LD A,(FAR_RIGHT)
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  LD A,(NEAR_RIGHT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  SUB E
  LD D,A
  LD A,(BRANCH_END_TOP)
  LD E,A
  CALL DRAW_LINE

  LD A,(FAR_RIGHT)
  LD B,A
  LD A,(FAR_BOTTOM)
  LD C,A
  LD A,(NEAR_RIGHT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  SUB E
  LD D,A
  LD A,(BRANCH_END_BOTTOM)
  LD E,A
  CALL DRAW_LINE

  LD A,(BRANCH_OUTER)
  CALL LOAD_FAR_RECT
  CALL DRAW_RIGHT_RECT_EDGE
  RET

DRAW_RIGHT_BRANCH_VISIBLE_CONTINUE:
  LD A,(RECT_INDEX)
  CP MAX_DEPTH
  RET NC
  INC A
  PUSH AF
  CALL LOAD_FAR_RECT
  POP AF
  DEC A
  CALL LOAD_NEAR_RECT

  CALL COMPUTE_BRANCH_END_BOUNDS
  LD A,(FAR_RIGHT)
  LD B,A
  LD A,(FAR_TOP)
  LD C,A
  LD A,(NEAR_RIGHT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  SUB E
  LD D,A
  LD A,(BRANCH_END_TOP)
  LD E,A
  CALL DRAW_LINE

  LD A,(FAR_RIGHT)
  LD B,A
  LD A,(FAR_BOTTOM)
  LD C,A
  LD A,(NEAR_RIGHT)
  LD D,A
  LD A,(BRANCH_END_GAP)
  LD E,A
  LD A,D
  SUB E
  LD D,A
  LD A,(BRANCH_END_BOTTOM)
  LD E,A
  CALL DRAW_LINE
  RET

