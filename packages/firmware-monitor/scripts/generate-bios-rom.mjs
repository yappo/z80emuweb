import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { assemble } from '@z80emu/assembler-z80';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const packageRoot = path.resolve(__dirname, '..');
const asmDir = path.resolve(packageRoot, 'asm');
const biosFile = path.resolve(asmDir, 'bios.asm');
const outFile = path.resolve(packageRoot, 'src/generated/bios_rom.ts');
const fontSourceFile = path.resolve(packageRoot, '..', 'machine-pcg815', 'src', 'font5x7.ts');
const fontAsmFile = path.resolve(asmDir, 'generated_font5x7.asm');

function parseFontRows(source) {
  const rowsByCode = new Map();
  const entryPattern = /\[(0x[0-9a-f]+|\d+),\s*glyph\(([^)]*)\)\]/gi;
  for (const match of source.matchAll(entryPattern)) {
    const code = Number(match[1]);
    const rowPattern = /'([01]{5})'/g;
    const rows = [...match[2].matchAll(rowPattern)].map((rowMatch) => rowMatch[1]);
    if (rows.length === 7) {
      rowsByCode.set(code & 0xff, rows);
    }
  }
  return rowsByCode;
}

function buildColumnBytes(rows) {
  const columns = [];
  for (let x = 0; x < 5; x += 1) {
    let bits = 0;
    for (let y = 0; y < 7; y += 1) {
      if ((rows[y] ?? '00000')[x] === '1') {
        bits |= 1 << y;
      }
    }
    columns.push(bits & 0xff);
  }
  return columns;
}

function generateFontAsm() {
  const rowsByCode = parseFontRows(fs.readFileSync(fontSourceFile, 'utf8'));
  const lines = ['; Auto-generated by scripts/generate-bios-rom.mjs', 'FONT5X7_TABLE:'];
  for (let code = 0; code <= 0xff; code += 1) {
    const rows = rowsByCode.get(code) ?? ['00000', '00000', '00000', '00000', '00000', '00000', '00000'];
    const columns = buildColumnBytes(rows).map((value) => `0x${value.toString(16).padStart(2, '0')}`);
    lines.push(`  DB ${columns.join(', ')} ; 0x${code.toString(16).padStart(2, '0')}`);
  }
  fs.writeFileSync(fontAsmFile, `${lines.join('\n')}\n`, 'utf8');
}

function symbolValue(result, name) {
  const symbol = result.symbols.find((entry) => entry.name === name);
  if (!symbol) {
    throw new Error(`Missing BIOS symbol: ${name}`);
  }
  return symbol.value & 0xffff;
}

generateFontAsm();

const result = assemble(fs.readFileSync(biosFile, 'utf8'), {
  filename: biosFile,
  addressRange: { start: 0x0000, end: 0xffff },
  includeResolver: (fromFilename, includePath) => {
    const baseDir = path.dirname(fromFilename);
    const resolved = path.resolve(baseDir, includePath);
    if (!resolved.startsWith(asmDir) || !fs.existsSync(resolved)) {
      return undefined;
    }
    return {
      filename: resolved,
      source: fs.readFileSync(resolved, 'utf8')
    };
  }
});

if (!result.ok) {
  const detail = result.diagnostics.map((d) => `${d.file}:${d.line}:${d.column}: ${d.message}`).join('\n');
  throw new Error(`BIOS assemble failed\n${detail}`);
}

const symbolNames = [
  'BIOS_JUMP_TABLE_START',
  'BIOS_BOOT_ENTRY',
  'BIOS_COLD_BOOT_ADDR',
  'BIOS_MONITOR_MAIN_LOOP_ENTRY',
  'BIOS_CHAR_PUT_ENTRY',
  'BIOS_CHAR_REPEAT_ENTRY',
  'BIOS_STRING_PUT_ENTRY',
  'BIOS_GRAPHICS_WRITE_RAW_ENTRY',
  'BIOS_CRLF_ENTRY',
  'BIOS_CLEAR_SCREEN_ENTRY',
  'BIOS_SET_CURSOR_ENTRY',
  'BIOS_GET_GLYPH_PTR_ENTRY',
  'BIOS_INKEY_ENTRY',
  'BIOS_WAIT_KEY_ENTRY',
  'BIOS_GET_DISPLAY_START_ENTRY',
  'BIOS_SET_DISPLAY_START_ENTRY',
  'BIOS_SHUTDOWN_ENTRY',
  'MONITOR_PROMPT_RESUME_ADDR',
  'MONITOR_MAIN_LOOP_ADDR'
];

const bytes = [...result.binary].map((b) => `0x${b.toString(16).padStart(2, '0')}`);
const chunks = [];
for (let i = 0; i < bytes.length; i += 16) {
  chunks.push(`  ${bytes.slice(i, i + 16).join(', ')}`);
}

const symbolLines = symbolNames.map((name) => `export const ${name} = 0x${symbolValue(result, name).toString(16).toUpperCase()};`);

const output = `// Auto-generated by scripts/generate-bios-rom.mjs
// PC-G815 BIOS ROM image

export const BIOS_ROM_BASE = 0x8000;
export const BIOS_JUMP_TABLE_ENTRY_COUNT = 30;
export const BIOS_RESERVED_NOP_BYTES = 3;

${symbolLines.join('\n')}

export const BIOS_ROM_IMAGE = Uint8Array.from([
${chunks.join(',\n')}
]);
`;

fs.writeFileSync(outFile, output, 'utf8');
console.log(`Generated ${outFile} (${result.binary.length} bytes)`);
