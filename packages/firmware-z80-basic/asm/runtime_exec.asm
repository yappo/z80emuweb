  JR DELETE_LINE_LOOP
DELETE_LINE_SKIP:
  LD B,0
DELETE_SKIP_BYTES:
  LD A,B
  OR C
  JR Z,DELETE_LINE_LOOP
  INC HL
  DEC BC
  JR DELETE_SKIP_BYTES
