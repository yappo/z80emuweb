; ============================================================================
; 実行ループ / 命令ディスパッチ
; ----------------------------------------------------------------------------
; 役割:
;   - 入力行を保存または即時実行へ振り分ける。
;   - RUN 時は ProgramArea から各行を取り出して Z80 上で解釈実行する。
; ============================================================================

TOKEN_THEN:
  DB "THEN",0
TOKEN_TO:
  DB "TO",0
TOKEN_STEP:
  DB "STEP",0
TOKEN_USING:
  DB "USING",0

; ----------------------------------------------------------------------------
; ルーチン: BASIC_MAIN_ENTRY
; 役割: インタープリターのエントリーポイント。
; ----------------------------------------------------------------------------
BASIC_MAIN_ENTRY:
  CALL INIT_INTERPRETER

BASIC_MAIN_LOOP:
  CALL READ_LINE
  LD A,(RAM_FLAG_EOT)
  OR A
  RET NZ

  CALL PROCESS_LINE
  JR BASIC_MAIN_LOOP

; ----------------------------------------------------------------------------
; ルーチン: INIT_INTERPRETER
; 役割: 起動時初期化。
; ----------------------------------------------------------------------------
INIT_INTERPRETER:
  CALL CLEAR_PROGRAM
  CALL CLEAR_VARIABLE_TABLE
  CALL CLEAR_FILE_TABLE
  XOR A
  LD (RAM_LINE_LEN),A
  LD (RAM_FLAG_EOT),A
  LD (RAM_EXEC_STOP),A
  LD (RAM_JUMP_FLAG),A
  LD (RAM_PTR_OVERRIDE),A
  LD (RAM_AUTO_FLAG),A
  LD (RAM_PAUSE_FLAG),A
  LD (RAM_REPEAT_FLAG),A
  LD (RAM_DATA_COUNT),A
  LD (RAM_DATA_INDEX),A
  LD (RAM_LABEL_CACHE_VALID),A
  LD (RAM_TEXT_COL),A
  LD A,0x2B
  LD (RAM_RND_SEED_LO),A
  LD A,0x4D
  LD (RAM_RND_SEED_HI),A
  RET

; ----------------------------------------------------------------------------
; ルーチン: PROCESS_LINE
; 役割: 入力済み行を保存または実行へ振り分ける。
; ----------------------------------------------------------------------------
PROCESS_LINE:
  LD A,(RAM_LINE_LEN)
  OR A
  RET Z

  LD HL,RAM_LINEBUF
  CALL SKIP_SPACES
  CALL SET_CMDPTR_FROM_HL

  LD A,(HL)
  OR A
  RET Z

  CALL IS_DIGIT
  JR C,PROCESS_PROGRAM_LINE
  CP '*'
  JR Z,PROCESS_PROGRAM_LINE

  CALL EXECUTE_IMMEDIATE
  RET

PROCESS_PROGRAM_LINE:
  CALL APPEND_PROGRAM_LINE
  RET

; ----------------------------------------------------------------------------
; ルーチン: EXECUTE_IMMEDIATE
; 役割: 即時入力行を1文として実行する。
; ----------------------------------------------------------------------------
EXECUTE_IMMEDIATE:
  CALL EXECUTE_COMMAND_AT_CMDPTR
  RET

; ----------------------------------------------------------------------------
; ルーチン: RUN_STORED_PROGRAM
; 役割: ProgramArea を先頭から順次実行する。
; ----------------------------------------------------------------------------
RUN_STORED_PROGRAM:
  XOR A
  LD (RAM_EXEC_STOP),A
  LD (RAM_JUMP_FLAG),A
  LD (RAM_PTR_OVERRIDE),A
  LD (RAM_LAST_ERROR),A

  LD A,0x00
  LD (RAM_RUN_PTR_LO),A
  LD A,0x40
  LD (RAM_RUN_PTR_HI),A
  XOR A
  LD (RAM_TMP_VAL_LO),A
  LD (RAM_TMP_VAL_HI),A

RUN_PROGRAM_MAIN_LOOP:
  CALL IS_RUN_PTR_AT_END
  RET Z

  CALL LOAD_CURRENT_RECORD
  CALL EXECUTE_COMMAND_AT_CMDPTR

  LD A,(RAM_EXEC_STOP)
  OR A
  RET NZ

  LD A,(RAM_PTR_OVERRIDE)
  OR A
  JR Z,RUN_PROGRAM_CHECK_JUMP
  XOR A
  LD (RAM_PTR_OVERRIDE),A
  LD A,(RAM_OVERRIDE_LO)
  LD (RAM_RUN_PTR_LO),A
  LD A,(RAM_OVERRIDE_HI)
  LD (RAM_RUN_PTR_HI),A
  JR RUN_PROGRAM_MAIN_LOOP

RUN_PROGRAM_CHECK_JUMP:
  LD A,(RAM_JUMP_FLAG)
  OR A
  JR Z,RUN_PROGRAM_NEXT_LINEAR
  XOR A
  LD (RAM_JUMP_FLAG),A
  LD A,(RAM_JUMP_LINE_LO)
  LD E,A
  LD A,(RAM_JUMP_LINE_HI)
  LD D,A
  CALL FIND_LINE_BY_NUMBER
  JR C,RUN_PROGRAM_MAIN_LOOP
  LD A,2
  LD (RAM_LAST_ERROR),A
  RET

RUN_PROGRAM_NEXT_LINEAR:
  LD A,(RAM_NEXT_PTR_LO)
  LD (RAM_RUN_PTR_LO),A
  LD A,(RAM_NEXT_PTR_HI)
  LD (RAM_RUN_PTR_HI),A
  JR RUN_PROGRAM_MAIN_LOOP

; ----------------------------------------------------------------------------
; ルーチン: IS_RUN_PTR_AT_END
; 役割: 現在実行ポインタが末尾に到達したか判定する。
; 出力: Z=1 到達済み, Z=0 継続
; ----------------------------------------------------------------------------
IS_RUN_PTR_AT_END:
  LD A,(RAM_RUN_PTR_LO)
  LD E,A
  LD A,(RAM_RUN_PTR_HI)
  LD D,A
  LD A,(RAM_PROG_PTR_LO)
  CP E
  JR NZ,IS_RUN_PTR_AT_END_NO
  LD A,(RAM_PROG_PTR_HI)
  CP D
  JR NZ,IS_RUN_PTR_AT_END_NO
  XOR A
  RET
IS_RUN_PTR_AT_END_NO:
  LD A,1
  OR A
  RET

; ----------------------------------------------------------------------------
; ルーチン: LOAD_CURRENT_RECORD
; 役割:
;   - RAM_RUN_PTR が指すレコードを RAM_LINEBUF へコピーする。
;   - RAM_NEXT_PTR に次レコード先頭を保存する。
;   - RAM_CMDPTR を「行番号の後ろ(文頭)」へ設定する。
; ----------------------------------------------------------------------------
LOAD_CURRENT_RECORD:
  LD A,(RAM_RUN_PTR_LO)
  LD E,A
  LD A,(RAM_RUN_PTR_HI)
  LD D,A

  LD A,(DE)
  LD B,A
  INC DE

  LD HL,RAM_LINEBUF
LOAD_CURRENT_RECORD_COPY:
  LD A,B
  OR A
  JR Z,LOAD_CURRENT_RECORD_COPY_DONE
  LD A,(DE)
  LD (HL),A
  INC DE
  INC HL
  DEC B
  JR LOAD_CURRENT_RECORD_COPY

LOAD_CURRENT_RECORD_COPY_DONE:
  XOR A
  LD (HL),A

  LD A,E
  LD (RAM_NEXT_PTR_LO),A
  LD A,D
  LD (RAM_NEXT_PTR_HI),A

  LD HL,RAM_LINEBUF
  CALL SKIP_SPACES
  CALL PARSE_LINE_NUMBER_FROM_HL
  JR NC,LOAD_CURRENT_RECORD_NO_LINE
  CALL SKIP_SPACES
LOAD_CURRENT_RECORD_NO_LINE:
  CALL SET_CMDPTR_FROM_HL
  RET

; ----------------------------------------------------------------------------
; ルーチン: FIND_LINE_BY_NUMBER
; 役割: ProgramArea から行番号 DE に一致するレコードを探索する。
; 出力: Carry=1 発見, RAM_RUN_PTR を更新
;       Carry=0 未発見
; ----------------------------------------------------------------------------
FIND_LINE_BY_NUMBER:
  LD A,E
  LD (RAM_TMP_VAL_LO),A
  LD A,D
  LD (RAM_TMP_VAL_HI),A
  LD A,(RAM_FIND_CACHE_VALID)
  OR A
  JR Z,FIND_LINE_SCAN_FROM_TOP
  LD A,(RAM_FIND_CACHE_LINE_LO)
  CP E
  JR NZ,FIND_LINE_SCAN_FROM_TOP
  LD A,(RAM_FIND_CACHE_LINE_HI)
  CP D
  JR NZ,FIND_LINE_SCAN_FROM_TOP
  LD A,(RAM_FIND_CACHE_PTR_LO)
  LD (RAM_RUN_PTR_LO),A
  LD A,(RAM_FIND_CACHE_PTR_HI)
  LD (RAM_RUN_PTR_HI),A
  SCF
  RET

FIND_LINE_SCAN_FROM_TOP:
  LD BC,RAM_PROG_START
FIND_LINE_LOOP:
  LD A,(RAM_PROG_PTR_LO)
  CP C
  JR NZ,FIND_LINE_HAS_RECORD
  LD A,(RAM_PROG_PTR_HI)
  CP B
  JR Z,FIND_LINE_NOT_FOUND

FIND_LINE_HAS_RECORD:
  LD A,(BC)
  LD E,A
  INC BC
  PUSH BC
  PUSH DE
  LD H,B
  LD L,C
  CALL PARSE_LINE_NUMBER_FROM_HL
  JR NC,FIND_LINE_SKIP_COMPARE

  LD A,(RAM_TMP_VAL_LO)
  CP E
  JR NZ,FIND_LINE_SKIP_COMPARE
  LD A,(RAM_TMP_VAL_HI)
  CP D
  JR NZ,FIND_LINE_SKIP_COMPARE

  POP DE
  POP BC
  DEC BC
  LD A,C
  LD (RAM_RUN_PTR_LO),A
  LD A,B
  LD (RAM_RUN_PTR_HI),A
  LD A,1
  LD (RAM_FIND_CACHE_VALID),A
  LD A,(RAM_TMP_VAL_LO)
  LD (RAM_FIND_CACHE_LINE_LO),A
  LD A,(RAM_TMP_VAL_HI)
  LD (RAM_FIND_CACHE_LINE_HI),A
  LD A,C
  LD (RAM_FIND_CACHE_PTR_LO),A
  LD A,B
  LD (RAM_FIND_CACHE_PTR_HI),A
  SCF
  RET

FIND_LINE_SKIP_COMPARE:
  POP DE
  POP BC
  DEC BC

  ; BC = BC + 1 + length
  LD HL,1
  ADD HL,BC
  LD A,E
  LD E,A
  LD D,0
  ADD HL,DE
  LD B,H
  LD C,L
  JR FIND_LINE_LOOP

FIND_LINE_NOT_FOUND:
  AND A
  RET

; ----------------------------------------------------------------------------
; ルーチン: EXECUTE_COMMAND_AT_CMDPTR
; 役割: RAM_CMDPTR の1文を分類し、対応ハンドラを呼ぶ。
; ----------------------------------------------------------------------------
EXECUTE_COMMAND_AT_CMDPTR:
  CALL GET_CMDPTR_HL
  CALL SKIP_SPACES
  CALL SET_CMDPTR_FROM_HL
  LD A,(HL)
  OR A
  RET Z
  CP '*'
  JR Z,EXECUTE_SKIP_LABEL_DECL
  CP '.'
  JR NZ,EXECUTE_COMMAND_CLASSIFY
  INC HL
  LD A,(HL)
  OR A
  RET Z
  DEC HL

EXECUTE_COMMAND_CLASSIFY:
  CALL CLASSIFY_COMMAND
  OR A
  JR NZ,EXECUTE_DISPATCH

  ; キーワードで始まらない場合は暗黙 LET を試みる。
  CALL TRY_IMPLICIT_ASSIGNMENT
  RET

EXECUTE_SKIP_LABEL_DECL:
  INC HL
EXECUTE_SKIP_LABEL_DECL_LOOP:
  LD A,(HL)
  OR A
  RET Z
  CP ':'
  JR Z,EXECUTE_SKIP_LABEL_DECL_DONE
  INC HL
  JR EXECUTE_SKIP_LABEL_DECL_LOOP
EXECUTE_SKIP_LABEL_DECL_DONE:
  INC HL
  CALL SKIP_SPACES
  CALL SET_CMDPTR_FROM_HL
  LD A,(HL)
  OR A
  RET Z
  JR EXECUTE_COMMAND_CLASSIFY

EXECUTE_TRY_ABNORMAL_TAIL:
  CALL LOAD_AFTER_CMD_HL
  CALL SKIP_SPACES
  LD A,(HL)
  CP '?'
  JR NZ,EXECUTE_TRY_ABNORMAL_NO
  INC HL
  LD A,(HL)
  CP '?'
  JR NZ,EXECUTE_TRY_ABNORMAL_NO
  INC HL
  LD A,(HL)
  CP '?'
  JR NZ,EXECUTE_TRY_ABNORMAL_NO
  INC HL
  CALL SKIP_SPACES
  LD A,(HL)
  OR A
  JR NZ,EXECUTE_TRY_ABNORMAL_NO
  SCF
  RET
EXECUTE_TRY_ABNORMAL_NO:
  AND A
  RET

EXECUTE_DISPATCH:
  PUSH AF
  CALL EXECUTE_TRY_ABNORMAL_TAIL
  JR NC,EXECUTE_DISPATCH_NORMAL
  POP AF
  RET
