  LD A,C
  LD (DE),A
  INC DE
  LD B,0
DELETE_COPY_BYTES:
  LD A,B
  OR C
  JR Z,DELETE_LINE_NEXT
  LD A,(HL)
  LD (DE),A
  INC HL
  INC DE
  DEC BC
  JR DELETE_COPY_BYTES
