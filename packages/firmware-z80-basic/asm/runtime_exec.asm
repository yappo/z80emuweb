; ----------------------------------------------------------------------------
; ルーチン: TRY_IMPLICIT_ASSIGNMENT
; 役割: キーワード不一致時に "A=1" 形式の代入を試行する。
; ----------------------------------------------------------------------------
TRY_IMPLICIT_ASSIGNMENT:
  CALL GET_CMDPTR_HL
  CALL EXPR_SET_PTR_HL
  JP EXEC_ASSIGNMENT_FROM_EXPRPTR

; ----------------------------------------------------------------------------
; 命令: NEW
; ----------------------------------------------------------------------------
EXEC_CMD_NEW:
  CALL ASSERT_END_AFTER_CMD
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  CALL CLEAR_PROGRAM
  CALL CLEAR_VARIABLE_TABLE
  RET

; ----------------------------------------------------------------------------
; 命令: LIST
; ----------------------------------------------------------------------------
EXEC_CMD_LIST:
  CALL ASSERT_END_AFTER_CMD
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  CALL LIST_PROGRAM
  RET

; ----------------------------------------------------------------------------
; 命令: RUN
; ----------------------------------------------------------------------------
EXEC_CMD_RUN:
  CALL ASSERT_END_AFTER_CMD
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  LD A,0x80
  OUT (LCD_CMD_PORT),A
  XOR A
  LD (RAM_TEXT_COL),A
  CALL RUN_STORED_PROGRAM
  RET

; ----------------------------------------------------------------------------
; 命令: PRINT
; ----------------------------------------------------------------------------
EXEC_CMD_PRINT:
  CALL LOAD_AFTER_CMD_HL
  CALL EXPR_SET_PTR_HL
  XOR A
  LD (RAM_DATA_SCAN_LO),A  ; PRINT内の列位置
  LD (RAM_DATA_SCAN_HI),A  ; PRINT内の項目数

  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  OR A
  JP Z,PRINT_CRLF
  LD DE,TOKEN_USING
  CALL MATCH_TOKEN
  JP C,PRINT_USING_MODE

PRINT_ITEM_LOOP:
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP ';'
  JP Z,SET_SYNTAX_ERROR
  CP ','
  JP Z,SET_SYNTAX_ERROR
  CP '"'
  JR Z,PRINT_QUOTED

  CALL PRINT_TRY_CHAR_FUNCTION
  JR NC,PRINT_EVAL_NUMBER
  ; CHAR$/CHR$ は1文字出力
  LD A,(RAM_DATA_SCAN_LO)
  INC A
  LD (RAM_DATA_SCAN_LO),A
  LD A,(RAM_DATA_SCAN_HI)
  INC A
  LD (RAM_DATA_SCAN_HI),A
  JR PRINT_DELIM

PRINT_EVAL_NUMBER:
  CALL EVAL_EXPRESSION
  PUSH HL
  CALL PRINT_NUMBER_LEN_HL
  LD B,A
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  LD C,A
  POP HL

  ; 項目が1つの式の場合は12桁内で右詰め。
  LD A,(RAM_DATA_SCAN_HI)
  OR A
  JR NZ,PRINT_NUMBER_DO_OUTPUT
  LD A,C
  OR A
  JR Z,PRINT_NUMBER_ALIGN_FIELD
  CP ';'
  JR NZ,PRINT_NUMBER_DO_OUTPUT
  PUSH BC
  PUSH HL
  INC HL
  CALL SKIP_SPACES
  LD A,(HL)
  POP HL
  POP BC
  OR A
  JR NZ,PRINT_NUMBER_DO_OUTPUT

PRINT_NUMBER_ALIGN_FIELD:
  LD A,(RAM_TEXT_COL)
  LD C,A
  LD A,24
  SUB C
  SUB B
  JR C,PRINT_NUMBER_DO_OUTPUT
  JR Z,PRINT_NUMBER_DO_OUTPUT
  LD B,A
  CALL PRINT_SPACES_COUNT_B

PRINT_NUMBER_DO_OUTPUT:
  PUSH BC
  CALL PRINT_NUMBER_HL
  POP BC
  LD A,(RAM_DATA_SCAN_LO)
  ADD A,B
  LD (RAM_DATA_SCAN_LO),A
  LD A,(RAM_DATA_SCAN_HI)
  INC A
  LD (RAM_DATA_SCAN_HI),A
  JR PRINT_DELIM

PRINT_QUOTED:
  INC HL
PRINT_QUOTED_LOOP:
  LD A,(HL)
  OR A
  JR Z,PRINT_DELIM_END
  CP '"'
  JR Z,PRINT_QUOTED_DONE
  CALL OUT_LCD_CHAR
  LD A,(RAM_DATA_SCAN_LO)
  INC A
  LD (RAM_DATA_SCAN_LO),A
  INC HL
  JR PRINT_QUOTED_LOOP

PRINT_QUOTED_DONE:
  INC HL
  LD A,(RAM_DATA_SCAN_HI)
  INC A
  LD (RAM_DATA_SCAN_HI),A
PRINT_DELIM_END:
  CALL EXPR_SET_PTR_HL

PRINT_DELIM:
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  OR A
  JP Z,PRINT_ENDLINE_IF_NEEDED
  CP ';'
  JR Z,PRINT_DELIM_SEMI
  CP ','
  JR Z,PRINT_DELIM_COMMA
  JP PRINT_CRLF

PRINT_DELIM_SEMI:
  INC HL
  CALL EXPR_SET_PTR_HL
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  OR A
  RET Z
  JP PRINT_ITEM_LOOP

PRINT_DELIM_COMMA:
  CALL PRINT_TAB_TO_NEXT_12
  INC HL
  CALL EXPR_SET_PTR_HL
  JP PRINT_ITEM_LOOP

PRINT_ENDLINE_IF_NEEDED:
  LD A,(RAM_DATA_SCAN_HI)
  OR A
  JP Z,PRINT_CRLF
  LD A,(RAM_TEXT_COL)
  OR A
  RET Z
  JP PRINT_CRLF

PRINT_USING_MODE:
  CALL EXPR_SET_PTR_HL
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP '"'
  JP NZ,SET_SYNTAX_ERROR
PRINT_USING_SKIP_FMT:
  INC HL
  LD A,(HL)
  OR A
  JP Z,SET_SYNTAX_ERROR
  CP '"'
  JR NZ,PRINT_USING_SKIP_FMT
  INC HL
  LD A,(HL)
  CP ';'
  JP NZ,SET_SYNTAX_ERROR
  INC HL
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  CALL PRINT_NUMBER_HL
  JP PRINT_CRLF

; ----------------------------------------------------------------------------
; ルーチン: PRINT_TRY_CHAR_FUNCTION
; 役割: PRINT項目先頭の CHR$(...) / CHAR$(...) を文字出力として処理する。
; 出力: Carry=1 処理済み, Carry=0 非該当
; ----------------------------------------------------------------------------
PRINT_TRY_CHAR_FUNCTION:
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL

  ; C...
  LD A,(HL)
  CALL TO_UPPER
  CP 'C'
  JR NZ,PRINT_TRY_CHAR_NONE
  INC HL
  ; CH...
  LD A,(HL)
  CALL TO_UPPER
  CP 'H'
  JR NZ,PRINT_TRY_CHAR_NONE
  INC HL
  ; CHR$ or CHAR$
  LD A,(HL)
  CALL TO_UPPER
  CP 'A'
  JR Z,PRINT_TRY_CHAR_CHAR_PATH
  CP 'R'
  JR NZ,PRINT_TRY_CHAR_NONE
  INC HL
  LD A,(HL)
  CP '$'
  JR NZ,PRINT_TRY_CHAR_NONE
  JR PRINT_TRY_CHAR_PARSE_ARGS

PRINT_TRY_CHAR_CHAR_PATH:
  INC HL
  LD A,(HL)
  CALL TO_UPPER
  CP 'R'
  JR NZ,PRINT_TRY_CHAR_NONE
  INC HL
  LD A,(HL)
  CP '$'
  JR NZ,PRINT_TRY_CHAR_NONE

PRINT_TRY_CHAR_PARSE_ARGS:
  INC HL
  CALL SKIP_SPACES
  LD A,(HL)
  CP '('
  JR Z,PRINT_TRY_CHAR_HAVE_PAREN
  CALL SET_SYNTAX_ERROR
  SCF
  RET

PRINT_TRY_CHAR_HAVE_PAREN:
  INC HL
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  PUSH HL
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP ')'
  JR Z,PRINT_TRY_CHAR_CLOSE_OK
  POP HL
  CALL SET_SYNTAX_ERROR
  SCF
  RET

PRINT_TRY_CHAR_CLOSE_OK:
  INC HL
  CALL EXPR_SET_PTR_HL
  POP HL
  LD A,L
  CALL OUT_LCD_CHAR
  SCF
  RET

PRINT_TRY_CHAR_NONE:
  AND A
  RET

; ----------------------------------------------------------------------------
; ルーチン: PRINT_NUMBER_LEN_HL
; 役割: HL を10進表示したときの文字数を A に返す（符号込み）
; ----------------------------------------------------------------------------
PRINT_NUMBER_LEN_HL:
  PUSH BC
  PUSH DE
  PUSH HL
  LD B,0
  LD A,H
  AND 0x80
  JR Z,PRINT_NUMBER_LEN_POSITIVE
  INC B
  LD A,H
  CPL
  LD H,A
  LD A,L
  CPL
  LD L,A
  INC HL
PRINT_NUMBER_LEN_POSITIVE:
  LD A,H
  OR L
  JR NZ,PRINT_NUMBER_LEN_NONZERO
  INC B
  LD A,B
  POP HL
  POP DE
  POP BC
  RET
PRINT_NUMBER_LEN_NONZERO:
PRINT_NUMBER_LEN_LOOP:
  CALL DIV10_HL
  INC B
  LD A,H
  OR L
  JR NZ,PRINT_NUMBER_LEN_LOOP
  LD A,B
  POP HL
  POP DE
  POP BC
  RET

; ----------------------------------------------------------------------------
; ルーチン: PRINT_TAB_TO_NEXT_12
; 役割: 現在列から次の12桁境界まで空白を出力する。
; ----------------------------------------------------------------------------
PRINT_TAB_TO_NEXT_12:
  LD A,(RAM_DATA_SCAN_LO)
  LD B,12
PRINT_TAB_REDUCE:
  CP 12
  JR C,PRINT_TAB_HAVE_REMAINDER
  SUB 12
  JR PRINT_TAB_REDUCE
PRINT_TAB_HAVE_REMAINDER:
  LD C,A
  LD A,12
  SUB C
  LD B,A
  JR NZ,PRINT_TAB_HAVE_SPACES
  LD B,12
PRINT_TAB_HAVE_SPACES:
  CALL PRINT_SPACES_COUNT_B
  RET

; ----------------------------------------------------------------------------
; ルーチン: PRINT_SPACES_COUNT_B
; 役割: B個の空白を出力し、列位置を進める。
; ----------------------------------------------------------------------------
PRINT_SPACES_COUNT_B:
  LD A,B
  OR A
  RET Z
PRINT_SPACES_COUNT_B_LOOP:
  CALL PRINT_SPACE
  LD A,(RAM_DATA_SCAN_LO)
  INC A
  LD (RAM_DATA_SCAN_LO),A
  DJNZ PRINT_SPACES_COUNT_B_LOOP
  RET

; ----------------------------------------------------------------------------
; 命令: LET / 暗黙 LET
; ----------------------------------------------------------------------------
EXEC_CMD_LET:
  CALL LOAD_AFTER_CMD_HL
  CALL EXPR_SET_PTR_HL
  CALL EXEC_ASSIGNMENT_FROM_EXPRPTR
  RET

EXEC_ASSIGNMENT_FROM_EXPRPTR:
  XOR A
  LD (RAM_CMP_OP),A
  CALL PARSE_VARIABLE_KEY_FROM_EXPR
  JP NC,SET_SYNTAX_ERROR
  LD A,(RAM_TMP_KEY1)
  LD (RAM_ASSIGN_KEY1),A
  LD A,(RAM_TMP_KEY2)
  LD (RAM_ASSIGN_KEY2),A
  LD A,(RAM_TMP_KEY3)
  LD (RAM_ASSIGN_KEY3),A
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP '('
  JR NZ,EXEC_ASSIGNMENT_EQ_CHECK
  LD A,1
  LD (RAM_CMP_OP),A
  INC HL
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  LD A,H
  OR A
  JP NZ,SET_SYNTAX_ERROR
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP ')'
  RET NZ
  INC HL
  CALL EXPR_SET_PTR_HL
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
EXEC_ASSIGNMENT_EQ_CHECK:
  LD A,(HL)
  CP '='
  JP NZ,SET_SYNTAX_ERROR
  INC HL
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  LD A,(RAM_CMP_OP)
  OR A
  JR Z,EXEC_ASSIGNMENT_STORE_EVAL
  LD DE,7
  JR EXEC_ASSIGNMENT_STORE
EXEC_ASSIGNMENT_STORE_EVAL:
  LD D,H
  LD E,L
EXEC_ASSIGNMENT_STORE:
  ; 右辺評価中に RAM_TMP_KEY* が参照変数で上書きされるため、
  ; 代入先キーを退避領域から復元してから書き込む。
  LD A,(RAM_ASSIGN_KEY1)
  LD (RAM_TMP_KEY1),A
  LD A,(RAM_ASSIGN_KEY2)
  LD (RAM_TMP_KEY2),A
  LD A,(RAM_ASSIGN_KEY3)
  LD (RAM_TMP_KEY3),A
  CALL SET_VARIABLE_FROM_DE
  RET

; ----------------------------------------------------------------------------
; 命令: INPUT / LNINPUT
; 役割: 簡易実装として対象変数へ 0 を格納する。
; ----------------------------------------------------------------------------
EXEC_CMD_INPUT:
  CALL LOAD_AFTER_CMD_HL
  CALL EXPR_SET_PTR_HL
  CALL PARSE_VARIABLE_KEY_FROM_EXPR
  JP NC,SET_SYNTAX_ERROR
  CALL ASSERT_END_FROM_EXPRPTR
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  CALL READ_LINE
  LD A,(RAM_FLAG_EOT)
  OR A
  JR NZ,EXEC_CMD_INPUT_ZERO
  LD HL,RAM_LINEBUF
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  LD D,H
  LD E,L
  CALL SET_VARIABLE_FROM_DE
  RET
EXEC_CMD_INPUT_ZERO:
  LD DE,0
  CALL SET_VARIABLE_FROM_DE
  RET

EXEC_CMD_LNINPUT:
  CALL LOAD_AFTER_CMD_HL
  CALL EXPR_SET_PTR_HL
  CALL PARSE_VARIABLE_KEY_FROM_EXPR
  JP NC,SET_SYNTAX_ERROR
  CALL ASSERT_END_FROM_EXPRPTR
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  CALL READ_LINE
  LD A,(RAM_FLAG_EOT)
  OR A
  RET NZ
  LD HL,RAM_LINEBUF
EXEC_CMD_LNINPUT_ECHO:
  LD A,(HL)
  OR A
  JR Z,EXEC_CMD_LNINPUT_DONE
  CALL OUT_LCD_CHAR
  INC HL
  JR EXEC_CMD_LNINPUT_ECHO
EXEC_CMD_LNINPUT_DONE:
  CALL PRINT_CRLF
  RET

; ----------------------------------------------------------------------------
; 命令: GOTO
; ----------------------------------------------------------------------------
EXEC_CMD_GOTO:
  CALL LOAD_AFTER_CMD_HL
  CALL SKIP_SPACES
  LD A,(HL)
  CP '*'
  JR NZ,EXEC_CMD_GOTO_LINE_NUMBER
  CALL EXPR_SET_PTR_HL
  CALL PARSE_LABEL_REF_FROM_EXPRPTR
  JP NC,SET_SYNTAX_ERROR
  CALL ASSERT_END_FROM_EXPRPTR
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  CALL FIND_LABEL_PTR_BY_KEY
  JP NC,SET_SYNTAX_ERROR
  LD A,C
  LD (RAM_OVERRIDE_LO),A
  LD A,B
  LD (RAM_OVERRIDE_HI),A
  LD A,1
  LD (RAM_PTR_OVERRIDE),A
  RET

EXEC_CMD_GOTO_LINE_NUMBER:
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  LD A,L
  LD (RAM_JUMP_LINE_LO),A
  LD A,H
  LD (RAM_JUMP_LINE_HI),A
  LD A,1
  LD (RAM_JUMP_FLAG),A
  ; RUNAWAY 抑止: 現在行への自己 GOTO はエラー扱いにする。
  LD HL,RAM_LINEBUF
  CALL SKIP_SPACES
  CALL PARSE_LINE_NUMBER_FROM_HL
  JR NC,EXEC_CMD_GOTO_DONE
  LD A,(RAM_JUMP_LINE_LO)
  CP E
  JR NZ,EXEC_CMD_GOTO_DONE
  LD A,(RAM_JUMP_LINE_HI)
  CP D
  JR NZ,EXEC_CMD_GOTO_DONE
  LD A,7
  LD (RAM_LAST_ERROR),A
EXEC_CMD_GOTO_DONE:
  RET

; ----------------------------------------------------------------------------
; 命令: GOSUB
; ----------------------------------------------------------------------------
EXEC_CMD_GOSUB:
  LD A,(RAM_GOSUB_SP)
  CP GOSUB_MAX_DEPTH
  RET NC

  ; 返り先(次レコード)をスタックへ積む
  LD E,A
  ADD A,A
  LD E,A
  LD D,0
  LD HL,RAM_GOSUB_STACK
  ADD HL,DE
  LD A,(RAM_NEXT_PTR_LO)
  LD (HL),A
  INC HL
  LD A,(RAM_NEXT_PTR_HI)
  LD (HL),A

  LD A,(RAM_GOSUB_SP)
  INC A
  LD (RAM_GOSUB_SP),A

  CALL LOAD_AFTER_CMD_HL
  CALL SKIP_SPACES
  LD A,(HL)
  CP '*'
  JP NZ,EXEC_CMD_GOTO
  CALL EXPR_SET_PTR_HL
  CALL PARSE_LABEL_REF_FROM_EXPRPTR
  JP NC,SET_SYNTAX_ERROR
  CALL ASSERT_END_FROM_EXPRPTR
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  CALL FIND_LABEL_PTR_BY_KEY
  JP NC,SET_SYNTAX_ERROR
  LD A,C
  LD (RAM_OVERRIDE_LO),A
  LD A,B
  LD (RAM_OVERRIDE_HI),A
  LD A,1
  LD (RAM_PTR_OVERRIDE),A
  RET

; ----------------------------------------------------------------------------
; 命令: RETURN
; ----------------------------------------------------------------------------
EXEC_CMD_RETURN:
  LD A,(RAM_GOSUB_SP)
  OR A
  JP Z,SET_SYNTAX_ERROR
  DEC A
  LD (RAM_GOSUB_SP),A

  LD E,A
  ADD A,A
  LD E,A
  LD D,0
  LD HL,RAM_GOSUB_STACK
  ADD HL,DE
  LD A,(HL)
  LD (RAM_OVERRIDE_LO),A
  INC HL
  LD A,(HL)
  LD (RAM_OVERRIDE_HI),A
  LD A,1
  LD (RAM_PTR_OVERRIDE),A
  RET

; ----------------------------------------------------------------------------
; 命令: END / STOP
; ----------------------------------------------------------------------------
EXEC_CMD_END:
  CALL ASSERT_END_AFTER_CMD
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  LD A,1
  LD (RAM_EXEC_STOP),A
  RET

EXEC_CMD_STOP:
  CALL ASSERT_END_AFTER_CMD
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  LD A,(RAM_NEXT_PTR_LO)
  LD (RAM_PAUSE_PTR_LO),A
  LD A,(RAM_NEXT_PTR_HI)
  LD (RAM_PAUSE_PTR_HI),A
  LD A,1
  LD (RAM_PAUSE_FLAG),A
  JP EXEC_CMD_END

EXEC_CMD_CONT:
  CALL ASSERT_END_AFTER_CMD
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  LD A,(RAM_PAUSE_FLAG)
  OR A
  RET Z
  XOR A
  LD (RAM_PAUSE_FLAG),A
  LD (RAM_EXEC_STOP),A
  LD (RAM_PTR_OVERRIDE),A
  LD A,(RAM_PAUSE_PTR_LO)
  LD (RAM_RUN_PTR_LO),A
  LD A,(RAM_PAUSE_PTR_HI)
  LD (RAM_RUN_PTR_HI),A
  JP RUN_PROGRAM_MAIN_LOOP

; ----------------------------------------------------------------------------
; 命令: IF
; 形式: IF <expr><cmp><expr> THEN <line|statement>
; ----------------------------------------------------------------------------
EXEC_CMD_IF:
  CALL LOAD_AFTER_CMD_HL
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  PUSH HL

  CALL PARSE_COMPARATOR_FROM_EXPRPTR
  JR C,EXEC_CMD_IF_HAVE_CMP
  POP HL
  JP SET_SYNTAX_ERROR

EXEC_CMD_IF_HAVE_CMP:
  LD (RAM_CMP_OP),A
  CALL EVAL_EXPRESSION
  POP DE
  LD B,D
  LD C,E
  LD D,H
  LD E,L
  CALL CMP_BC_DE_UNSIGNED
  LD (RAM_TMP_VAL_LO),A

  LD A,(RAM_CMP_OP)
  CP CMP_EQ
  JR Z,IF_CHECK_EQ
  CP CMP_NE
  JR Z,IF_CHECK_NE
  CP CMP_LT
  JR Z,IF_CHECK_LT
  CP CMP_GT
  JR Z,IF_CHECK_GT
  CP CMP_LE
  JR Z,IF_CHECK_LE
  CP CMP_GE
  JR Z,IF_CHECK_GE
  RET

IF_CHECK_EQ:
  LD A,(RAM_TMP_VAL_LO)
  OR A
  JP Z,IF_THEN_EXEC
  JP IF_FIND_ELSE
IF_CHECK_NE:
  LD A,(RAM_TMP_VAL_LO)
  OR A
  JP NZ,IF_THEN_EXEC
  JP IF_FIND_ELSE
IF_CHECK_LT:
  LD A,(RAM_TMP_VAL_LO)
  CP 1
  JP Z,IF_THEN_EXEC
  JP IF_FIND_ELSE
IF_CHECK_GT:
  LD A,(RAM_TMP_VAL_LO)
  CP 2
  JP Z,IF_THEN_EXEC
  JP IF_FIND_ELSE
IF_CHECK_LE:
  LD A,(RAM_TMP_VAL_LO)
  CP 2
  JP NZ,IF_THEN_EXEC
  JP IF_FIND_ELSE
IF_CHECK_GE:
  LD A,(RAM_TMP_VAL_LO)
  CP 1
  JP NZ,IF_THEN_EXEC
  JP IF_FIND_ELSE

; ----------------------------------------------------------------------------
; ルーチン: CMP_BC_DE_UNSIGNED
; 役割: BC(lhs) と DE(rhs) を符号なし比較する。
; 出力: A=0(lhs==rhs), A=1(lhs<rhs), A=2(lhs>rhs)
; ----------------------------------------------------------------------------
CMP_BC_DE_UNSIGNED:
  LD A,B
  CP D
  JR C,CMP_BC_DE_LT
  JR NZ,CMP_BC_DE_GT
  LD A,C
  CP E
  JR C,CMP_BC_DE_LT
  JR NZ,CMP_BC_DE_GT
  XOR A
  RET
CMP_BC_DE_LT:
  LD A,1
  RET
CMP_BC_DE_GT:
  LD A,2
  RET

; ----------------------------------------------------------------------------
; ルーチン: PARSE_LABEL_KEY_FROM_HL
; 役割: HL位置からラベル名(先頭3文字)を抽出する。
; 出力: Carry=1 成功 / Carry=0 失敗, HL=ラベル末尾位置
; ----------------------------------------------------------------------------
PARSE_LABEL_KEY_FROM_HL:
  XOR A
  LD (RAM_TMP_KEY1),A
  LD (RAM_TMP_KEY2),A
  LD (RAM_TMP_KEY3),A
  LD B,0
PARSE_LABEL_KEY_LOOP:
  LD A,(HL)
  CALL TO_UPPER
  CALL IS_ALNUM
  JR C,PARSE_LABEL_KEY_ACCEPT
  CP '_'
  JR Z,PARSE_LABEL_KEY_ACCEPT
  JR PARSE_LABEL_KEY_DONE
PARSE_LABEL_KEY_ACCEPT:
  LD A,B
  CP 0
  JR NZ,PARSE_LABEL_KEY_SECOND
  LD A,(HL)
  CALL TO_UPPER
  LD (RAM_TMP_KEY1),A
  JR PARSE_LABEL_KEY_NEXT
PARSE_LABEL_KEY_SECOND:
  CP 1
  JR NZ,PARSE_LABEL_KEY_THIRD
  LD A,(HL)
  CALL TO_UPPER
  LD (RAM_TMP_KEY2),A
  JR PARSE_LABEL_KEY_NEXT
PARSE_LABEL_KEY_THIRD:
  CP 2
  JR NZ,PARSE_LABEL_KEY_NEXT
  LD A,(HL)
  CALL TO_UPPER
  LD (RAM_TMP_KEY3),A
PARSE_LABEL_KEY_NEXT:
  INC HL
  INC B
  JR PARSE_LABEL_KEY_LOOP
PARSE_LABEL_KEY_DONE:
  LD A,B
  OR A
  JR Z,PARSE_LABEL_KEY_FAIL
  SCF
  RET
PARSE_LABEL_KEY_FAIL:
  AND A
  RET

; ----------------------------------------------------------------------------
; ルーチン: PARSE_LABEL_REF_FROM_EXPRPTR
; 役割: 式ポインタ位置の *LABEL 参照を RAM_TMP_KEY* へ抽出する。
; ----------------------------------------------------------------------------
PARSE_LABEL_REF_FROM_EXPRPTR:
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP '*'
  JR NZ,PARSE_LABEL_REF_FAIL
  INC HL
  CALL PARSE_LABEL_KEY_FROM_HL
  JR NC,PARSE_LABEL_REF_FAIL
  CALL EXPR_SET_PTR_HL
  SCF
  RET
PARSE_LABEL_REF_FAIL:
  AND A
  RET

; ----------------------------------------------------------------------------
; ルーチン: FIND_LABEL_PTR_BY_KEY
; 役割: RAM_TMP_KEY* のラベル宣言行ポインタを BC へ返す（1件キャッシュ付）
; ----------------------------------------------------------------------------
FIND_LABEL_PTR_BY_KEY:
  LD A,(RAM_LABEL_CACHE_VALID)
  OR A
  JR Z,FIND_LABEL_SCAN_START
  LD A,(RAM_LABEL_CACHE_KEY1)
  LD D,A
  LD A,(RAM_TMP_KEY1)
  CP D
  JR NZ,FIND_LABEL_SCAN_START
  LD A,(RAM_LABEL_CACHE_KEY2)
  LD D,A
  LD A,(RAM_TMP_KEY2)
  CP D
  JR NZ,FIND_LABEL_SCAN_START
  LD A,(RAM_LABEL_CACHE_KEY3)
  LD D,A
  LD A,(RAM_TMP_KEY3)
  CP D
  JR NZ,FIND_LABEL_SCAN_START
  LD A,(RAM_LABEL_CACHE_PTR_LO)
  LD C,A
  LD A,(RAM_LABEL_CACHE_PTR_HI)
  LD B,A
  SCF
  RET

FIND_LABEL_SCAN_START:
  LD A,(RAM_TMP_KEY1)
  LD (RAM_ASSIGN_KEY1),A
  LD A,(RAM_TMP_KEY2)
  LD (RAM_ASSIGN_KEY2),A
  LD A,(RAM_TMP_KEY3)
  LD (RAM_ASSIGN_KEY3),A
  LD BC,RAM_PROG_START

FIND_LABEL_SCAN_LOOP:
  LD A,(RAM_PROG_PTR_LO)
  CP C
  JR NZ,FIND_LABEL_SCAN_HAS_RECORD
  LD A,(RAM_PROG_PTR_HI)
  CP B
  JR Z,FIND_LABEL_SCAN_NOT_FOUND

FIND_LABEL_SCAN_HAS_RECORD:
  PUSH BC
  LD H,B
  LD L,C
  INC HL
  CALL SKIP_SPACES
  CALL PARSE_LINE_NUMBER_FROM_HL
  JR NC,FIND_LABEL_SCAN_NO_LINE_NUMBER
  CALL SKIP_SPACES
FIND_LABEL_SCAN_NO_LINE_NUMBER:
  LD A,(HL)
  CP '*'
  JR NZ,FIND_LABEL_SCAN_NEXT_RECORD
  INC HL
  CALL PARSE_LABEL_KEY_FROM_HL
  JR NC,FIND_LABEL_SCAN_NEXT_RECORD
  LD A,(HL)
  CP ':'
  JR NZ,FIND_LABEL_SCAN_NEXT_RECORD

  LD A,(RAM_ASSIGN_KEY1)
  LD D,A
  LD A,(RAM_TMP_KEY1)
  CP D
  JR NZ,FIND_LABEL_SCAN_NEXT_RECORD
  LD A,(RAM_ASSIGN_KEY2)
  LD D,A
  LD A,(RAM_TMP_KEY2)
  CP D
  JR NZ,FIND_LABEL_SCAN_NEXT_RECORD
  LD A,(RAM_ASSIGN_KEY3)
  LD D,A
  LD A,(RAM_TMP_KEY3)
  CP D
  JR NZ,FIND_LABEL_SCAN_NEXT_RECORD

  POP BC
  LD A,1
  LD (RAM_LABEL_CACHE_VALID),A
  LD A,(RAM_ASSIGN_KEY1)
  LD (RAM_LABEL_CACHE_KEY1),A
  LD A,(RAM_ASSIGN_KEY2)
  LD (RAM_LABEL_CACHE_KEY2),A
  LD A,(RAM_ASSIGN_KEY3)
  LD (RAM_LABEL_CACHE_KEY3),A
  LD A,C
  LD (RAM_LABEL_CACHE_PTR_LO),A
  LD A,B
  LD (RAM_LABEL_CACHE_PTR_HI),A
  SCF
  RET

FIND_LABEL_SCAN_NEXT_RECORD:
  POP BC
  LD A,(BC)
  LD E,A
  INC BC
  LD D,0
  PUSH HL
  LD H,B
  LD L,C
  ADD HL,DE
  LD B,H
  LD C,L
  POP HL
  JR FIND_LABEL_SCAN_LOOP

FIND_LABEL_SCAN_NOT_FOUND:
  AND A
  RET

IF_FIND_ELSE:
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
IF_FIND_ELSE_LOOP:
  LD A,(HL)
  OR A
  RET Z
  CALL TO_UPPER
  CP 'E'
  JR NZ,IF_FIND_ELSE_NEXT
  INC HL
  LD A,(HL)
  CALL TO_UPPER
  CP 'L'
  JR NZ,IF_FIND_ELSE_BACK1
  INC HL
  LD A,(HL)
  CALL TO_UPPER
  CP 'S'
  JR NZ,IF_FIND_ELSE_BACK2
  INC HL
  LD A,(HL)
  CALL TO_UPPER
  CP 'E'
  JR NZ,IF_FIND_ELSE_BACK3
  INC HL
  CALL SET_CMDPTR_FROM_HL
  CALL EXECUTE_COMMAND_AT_CMDPTR
  RET
IF_FIND_ELSE_BACK3:
  DEC HL
IF_FIND_ELSE_BACK2:
  DEC HL
IF_FIND_ELSE_BACK1:
  DEC HL
IF_FIND_ELSE_NEXT:
  INC HL
  JR IF_FIND_ELSE_LOOP

IF_THEN_EXEC:
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD DE,TOKEN_THEN
  CALL MATCH_TOKEN
  JR C,IF_THEN_EXEC_HAVE_THEN
  ; THEN は省略可能（PC-G815仕様）
  CALL EXPR_SET_PTR_HL
  JR IF_THEN_EXEC_BODY
IF_THEN_EXEC_HAVE_THEN:
  CALL EXPR_SET_PTR_HL
IF_THEN_EXEC_BODY:
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP '*'
  JR Z,IF_THEN_LABEL_JUMP
  CALL IS_DIGIT
  JR NC,IF_THEN_STATEMENT

  CALL PARSE_LINE_NUMBER_FROM_HL
  RET NC
  LD A,E
  LD (RAM_JUMP_LINE_LO),A
  LD A,D
  LD (RAM_JUMP_LINE_HI),A
  LD A,1
  LD (RAM_JUMP_FLAG),A
  RET

IF_THEN_LABEL_JUMP:
  CALL EXPR_SET_PTR_HL
  CALL PARSE_LABEL_REF_FROM_EXPRPTR
  RET NC
  CALL ASSERT_END_FROM_EXPRPTR
  LD A,(RAM_LAST_ERROR)
  OR A
  RET NZ
  CALL FIND_LABEL_PTR_BY_KEY
  RET NC
  LD A,C
  LD (RAM_OVERRIDE_LO),A
  LD A,B
  LD (RAM_OVERRIDE_HI),A
  LD A,1
  LD (RAM_PTR_OVERRIDE),A
  RET

IF_THEN_STATEMENT:
  CALL SET_CMDPTR_FROM_HL
  CALL EXECUTE_COMMAND_AT_CMDPTR
  RET

; ----------------------------------------------------------------------------
; 命令: FOR
; 形式: FOR <var>=<expr> TO <expr> [STEP <expr>]
; ----------------------------------------------------------------------------
EXEC_CMD_FOR:
  LD A,(RAM_FOR_SP)
  CP FOR_MAX_DEPTH
  JP NC,SET_SYNTAX_ERROR

  CALL LOAD_AFTER_CMD_HL
  CALL EXPR_SET_PTR_HL
  CALL PARSE_VARIABLE_KEY_FROM_EXPR
  JP NC,SET_SYNTAX_ERROR

  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  CP '='
  JP NZ,SET_SYNTAX_ERROR
  INC HL
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  LD D,H
  LD E,L
  CALL SET_VARIABLE_FROM_DE

  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD DE,TOKEN_TO
  CALL MATCH_TOKEN
  JP NC,SET_SYNTAX_ERROR
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  LD A,L
  LD (RAM_TMP_VAL_LO),A
  LD A,H
  LD (RAM_TMP_VAL_HI),A

  LD DE,1
  CALL EXPR_SKIP_SPACES
  CALL EXPR_GET_PTR_HL
  LD A,(HL)
  OR A
  JR Z,FOR_HAVE_STEP
  LD DE,TOKEN_STEP
  CALL MATCH_TOKEN
  JR NC,FOR_HAVE_STEP
  CALL EXPR_SET_PTR_HL
  CALL EVAL_EXPRESSION
  LD D,H
  LD E,L

FOR_HAVE_STEP:
  ; フレーム先頭を HL に求める
  LD A,(RAM_FOR_SP)
  LD B,A
  LD HL,RAM_FOR_STACK
FOR_FRAME_ADDR_LOOP:
  LD A,B
  OR A
  JR Z,FOR_FRAME_ADDR_DONE
  LD DE,FOR_FRAME_SIZE
  ADD HL,DE
  DEC B
  JR FOR_FRAME_ADDR_LOOP
FOR_FRAME_ADDR_DONE:
  LD A,(RAM_TMP_KEY1)
  LD (HL),A
  INC HL
  LD A,(RAM_TMP_KEY2)
  LD (HL),A
  INC HL
  LD A,(RAM_TMP_KEY3)
  LD (HL),A
  INC HL
  LD A,(RAM_TMP_VAL_LO)
  LD (HL),A
  INC HL
  LD A,(RAM_TMP_VAL_HI)
  LD (HL),A
  INC HL
  LD A,E
  LD (HL),A
  INC HL
  LD A,D
  LD (HL),A
  INC HL
  LD A,(RAM_NEXT_PTR_LO)
  LD (HL),A
  INC HL
  LD A,(RAM_NEXT_PTR_HI)
  LD (HL),A

  LD A,(RAM_FOR_SP)
  INC A
  LD (RAM_FOR_SP),A
  RET

; ----------------------------------------------------------------------------
; 命令: NEXT
; 役割: 最上位 FOR フレームを更新して継続判定する。
; ----------------------------------------------------------------------------
EXEC_CMD_NEXT:
  LD A,(RAM_FOR_SP)
  OR A
  JP Z,SET_SYNTAX_ERROR
  DEC A
  LD B,A

  LD HL,RAM_FOR_STACK
NEXT_FRAME_ADDR_LOOP:
  LD A,B
  OR A
  JR Z,NEXT_FRAME_ADDR_DONE
  LD DE,FOR_FRAME_SIZE
  ADD HL,DE
  DEC B
  JR NEXT_FRAME_ADDR_LOOP
NEXT_FRAME_ADDR_DONE:
  ; key を RAM_TMP_KEY* へ
  LD A,(HL)
  LD (RAM_TMP_KEY1),A
  INC HL
  LD A,(HL)
  LD (RAM_TMP_KEY2),A
  INC HL
  LD A,(HL)
  LD (RAM_TMP_KEY3),A
  INC HL
  ; end をスタックへ退避
  LD E,(HL)   ; end lo
  INC HL
  LD D,(HL)   ; end hi
  PUSH DE
  INC HL
  ; step をスタックへ退避
  LD E,(HL)   ; step lo
  INC HL
  LD D,(HL)   ; step hi
  PUSH DE
  INC HL
  ; loop ptr
  LD A,(HL)
  LD (RAM_OVERRIDE_LO),A
  INC HL
  LD A,(HL)
  LD (RAM_OVERRIDE_HI),A

  ; step を取り出し、符号情報だけ AF で保持する
  POP BC              ; B=step hi, C=step lo
  LD A,B
  AND 0x80
  LD (RAM_CMP_OP),A

  ; new = cur + step
  PUSH BC
  CALL GET_VARIABLE_HL
  POP BC
  LD A,C
  ADD A,L
  LD E,A
  LD A,B
  ADC A,H
  LD D,A
  CALL SET_VARIABLE_FROM_DE

  ; compare: BC=new, DE=end
  LD B,D
  LD C,E
  POP DE              ; end
  CALL CMP_BC_DE_UNSIGNED
  LD B,A

  LD A,(RAM_CMP_OP)
  OR A
  JR NZ,NEXT_NEG_STEP
  ; 正方向: var <= end
  LD A,B
  CP 2
  JR NZ,NEXT_CONTINUE
  JR NEXT_POP_FRAME

NEXT_NEG_STEP:
  ; 負方向: var >= end
  LD A,B
  CP 1
  JR NZ,NEXT_CONTINUE
  JR NEXT_POP_FRAME

NEXT_CONTINUE:
  LD A,1
  LD (RAM_PTR_OVERRIDE),A
  RET

NEXT_POP_FRAME:
  LD A,(RAM_FOR_SP)
  DEC A
  LD (RAM_FOR_SP),A
  RET

