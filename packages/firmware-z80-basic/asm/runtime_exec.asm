  LD A,E
  LD (RAM_PROG_PTR_LO),A
  LD A,D
  LD (RAM_PROG_PTR_HI),A
  CALL INVALIDATE_FIND_LINE_CACHE
  XOR A
  LD (RAM_LABEL_CACHE_VALID),A
  RET

FIND_NEXT_WEND_PTR:
  LD A,(RAM_NEXT_PTR_LO)
  LD C,A
  LD A,(RAM_NEXT_PTR_HI)
  LD B,A
FIND_NEXT_WEND_LOOP:
  LD A,(RAM_PROG_PTR_LO)
  CP C
  JR NZ,FIND_NEXT_WEND_HAS_REC
  LD A,(RAM_PROG_PTR_HI)
  CP B
  JP Z,SET_SYNTAX_ERROR
FIND_NEXT_WEND_HAS_REC:
  LD A,(BC)
  LD E,A
  INC BC
  ; next pointer = BC + length
  LD H,B
  LD L,C
  LD D,0
  ADD HL,DE
  LD D,H
  LD E,L

  LD H,B
  LD L,C
  CALL SKIP_SPACES
  CALL PARSE_LINE_NUMBER_FROM_HL
  JR NC,FIND_NEXT_WEND_NO_LINE
  CALL SKIP_SPACES
FIND_NEXT_WEND_NO_LINE:
  CALL SET_CMDPTR_FROM_HL
  CALL CLASSIFY_COMMAND
  CP CMD_WEND
  JR Z,FIND_NEXT_WEND_FOUND

  LD C,E
  LD B,D
  JR FIND_NEXT_WEND_LOOP

FIND_NEXT_WEND_FOUND:
  LD A,E
  LD (RAM_OVERRIDE_LO),A
  LD A,D
  LD (RAM_OVERRIDE_HI),A
  RET
