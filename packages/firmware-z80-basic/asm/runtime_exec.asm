  LD HL,RAM_PROG_START
  LD DE,RAM_PROG_START
DELETE_LINE_LOOP:
  LD A,(RAM_PROG_PTR_LO)
  CP L
  JR NZ,DELETE_LINE_HAS_REC
  LD A,(RAM_PROG_PTR_HI)
  CP H
  JR Z,DELETE_LINE_DONE
DELETE_LINE_HAS_REC:
  LD A,(HL)
  LD C,A
  INC HL
  PUSH HL
  PUSH DE
  CALL PARSE_LINE_NUMBER_FROM_HL
  POP DE
  POP HL
  LD A,(RAM_TMP_VAL_LO)
  CP E
  JR NZ,DELETE_LINE_COPY
  LD A,(RAM_TMP_VAL_HI)
  CP D
  JR NZ,DELETE_LINE_COPY
  JR DELETE_LINE_SKIP
